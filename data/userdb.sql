  UserID INT NOT NULL PRIMARY KEY, 
  UserName VARCHAR(100) NOT NULL,
  Email VARCHAR(100) NOT NULL, 
  FullName VARCHAR(100) NOT NULL,
  Role VARCHAR(100) NOT NULL);

