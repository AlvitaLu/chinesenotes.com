  					<h4><a href='decorative_designs.php'>中国文物的纹饰 Decorative Designs in Chinese Art</a></h4>
  					<p>
  					  本文章描述的是淘气、青铜器以及其他文物的纹饰。纹饰与美术的区别是纹饰用可重复图案而带纹饰的文物有本身的作用。纹饰可以包涵中国文化的故事，有的无法仅用言语表达的。
  					</p>
  					<p>
  					  This article describes decorative art on pottery, bronze ware, and many other art works and artifacts. I distinguish decorative art from fine art in that decorative art is most often uses repeated designs and is applied to artifacts that have functions in themselves. Decorative art has a fascinating story to tell about Chinese culture that is not as easily told using words alone.
