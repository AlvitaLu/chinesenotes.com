      <p>
        In ancient times <span class='bookTitle'>bi</span> <a href='/word_detail.php?id=17552' onmouseover="showToolTip(this, 'bì​', 'a jade ceremonial disk')" 
        onmouseout="hideToolTip()">璧</a> (a jade disc) had a very high value.
        There is a story about a very valuable <span class='bookTitle'>bi</span> in the Warring States Period described in the book
        <span class='bookTitle'>Records of the Historian</span> by <span class='bookTitle'>Sima Qian</span> <a href='/word_detail.php?id=17476' onmouseover="showToolTip(this, '司馬遷 Sīmǎ Qiān', 'Sima Qian')" onmouseout="hideToolTip()">司马迁</a><a href='/word_detail.php?id=17475' onmouseover="showToolTip(this, '史記 Shǐjì​', 'Records of the Historian')" 
        onmouseout="hideToolTip()">史记</a>, written circa 100 BCE.
        The King of Qin offered the King of Zhao 15 cities in exchange for the <span class='bookTitle'>bi</span>.
        The King of Qin intended to cheat the King of Zhao but a Zhao official Lin Xiangru outsmarted the King of Qin.
        The <span class='bookTitle'>bi</span> shown below is from the Warring States Period.
      </p>
