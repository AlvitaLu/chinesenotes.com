        People of the time believed that jade had special powers that protected the body.
        Some rulers even had entire <span class='bookTitle'>jade burial suits</span> <a href='/word_detail.php?id=25779' onmouseover="showToolTip(this, 'yùyī', 'a jade burial suit')" 
