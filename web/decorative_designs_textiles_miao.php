      <p>
        <span class='bookTitle'>Miao costume</span> <a href='/word_detail.php?id=26220' onmouseover="showToolTip(this, 'miáofú', 'Miao costume')" 
        onmouseout='hideToolTip()'>苗服</a> is very diverse, including over 200 kinds.
        It is decorated with woven patterns, embroidery, and dyed designs.
        They are characteristically rich in color, as can be seen from the pictures on this page.
        Many of the designs are intended to identity of different groups or have other symbolic meaning.
        Miao clothing includes both formal attire and casual dress.
      </p>
