        <a href='/word_detail.php?id=25783' onmouseover="showToolTip(this, 'Miáo Zú', 'Miao')" onmouseout="hideToolTip()">苗族</a>
      </h3>
      <p>
        The <span class='bookTitle'>Miao people</span> <a href='/word_detail.php?id=25783' onmouseover="showToolTip(this, 'Miáo Zú', 'Miao')" 
        onmouseout='hideToolTip()'>苗族</a> number about 10 million and live in Southern China, mostly in Guizhou, Hunan, Yunnan, Sichuan, Guangxi, Hainan, 
        Guangdong, and Hubei provinces.
        Some Miao also live in Thailand, Laos, Vietnam and Myanmar.
        The Miao were among the first people to setting in the area that is now present-day China.
        Their origins can be traced back as least as far as the Shang Dynasty (1700&mdash;1045 BCE). 
      </p>
