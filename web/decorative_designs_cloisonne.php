      <h3 class='article'>
        <a name="cloisonne"></a>
        Cloisonne
        <a href='/word_detail.php?id=18949' onmouseover="showToolTip(this, '景泰藍 jǐng​tài​lán', 'cloisonne')" onmouseout="hideToolTip()">景泰蓝</a>
      </h3>
      <p>
        Cloisonne enamel techniques were brought to China from Persia during the Yuan Dynasty.
        The techniques were developed further in the Ming Dynasty and became widespread during the reign of
        seventh Ming Emperor <span class='bookTitle'>Jingtai</span> <a href='/word_detail.php?id=22855' onmouseover="showToolTip(this, 'Jǐngtài​', 'Emperor Jingtai')" 
        onmouseout='hideToolTip()'>景泰</a> (reigned 1449－1457)
        This is the origin of the Chinese name for <span class='bookTitle'>cloisonne</span> <span class='bookTitle'>Jingtailan</span> <a href='/word_detail.php?id=18949' onmouseover="showToolTip(this, '景泰藍 jǐng​tài​lán', 'cloisonne')" 
        onmouseout="hideToolTip()">景泰蓝</a>, with <span class='bookTitle'>lan</span> <a href='/word_detail.php?id=473' onmouseover="showToolTip(this, '藍 lán', 'blue')" 
        onmouseout="hideToolTip()">蓝</a> (blue) being the most common background color.
        The cloisonne items in the pictures below show this blue background color.
        To produce a cloisonne utensil, the artist first produces a copper roughcast, attaches some copper wires forming 
        decorative patterns, adds enamel between the spaces in the wires, and then fires the item in a kiln.
      </p>
