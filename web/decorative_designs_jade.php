      <h2 class='article'>
        <a name="jade"></a>
        Jade
        <a href='/word_detail.php?id=4843' onmouseover="showToolTip(this, 'yù​', 'jade')" onmouseout='hideToolTip()'>玉</a>
      </h2>
      <h3 class='article'>
        <a name="background"></a>
        Background
        <a href='/word_detail.php?id=1826' onmouseover="showToolTip(this, 'bèijǐng', 'background')" onmouseout='hideToolTip()'>背景</a>
      </h3>
      <p>
        Jade artwork in China dates from about 5,000 to 3,000 BCE, before the Bronze Age.
        It seems incredible that people could create these jade artifacts without metal tools, especially chisels.
        Because of of its beauty and unique qualities jade continued to be a favored medium for artwork long, especially jewelry,
        long after the heyday of painted pottery and bronze ware had passed.
        Chinese jade artwork is better known than jade artwork from any other country.
        The Shanghai Museum is one of the best galleries for Chinese jade and the exhibition, from which some of
        the photos here are taken, is stunning.
      </p>
      <p>
        Historically, <span class='bookTitle'>soft jade</span> <a href='/word_detail.php?id=25529' onmouseover="showToolTip(this, '軟玉 ruǎn yù', 'soft jade')" 
        onmouseout="hideToolTip()">软玉</a> was more commonly used that the much harder <span class='bookTitle'>jadeite</span>,
        which is as hard or harder than glass.
        Later on, tools and techniques were developed to work with <span class='bookTitle'>jadeite</span> and harder gemstones.
        <span class='bookTitle'>Nephrite</span> <a href='/word_detail.php?id=25528' onmouseover="showToolTip(this, 'hétián yù', 'nephrite')" 
        onmouseout="hideToolTip()">和田玉</a> is a kind of soft jade, and the term is often used synonymously with soft jade.
        The color of soft jade varies with the iron content varying from <span class='bookTitle'>white jade</span> <a href='/word_detail.php?id=25530' onmouseover="showToolTip(this, 'bái yù', 'white jade')" 
        onmouseout="hideToolTip()">白玉</a> to <span class='bookTitle'>green jade</span> <a href='/word_detail.php?id=25531' onmouseover="showToolTip(this, 'qīng yù', 'green jade')" 
        onmouseout="hideToolTip()">青玉</a>.
        Jadeite and other gemstones sometimes called jade have a greater variety of colors.
        There is considerable confusion over the exact terms for jade because of the use of the Chinese term for jade <a href='/word_detail.php?id=4843' onmouseover="showToolTip(this, 'yù​', 'jade')" 
        onmouseout='hideToolTip()'>玉</a> to mean precious stones in general and other historic use of Chinese terms for 
        varieties of jade
        For example, the Chinese term for jasper is <a href='/word_detail.php?id=25534' onmouseover="showToolTip(this, 'bìyù', 'jasper / fine jade')" 
        onmouseout='hideToolTip()'>碧玉</a>, which implies that it is a kind of jade  and, in fact, 
        it sometimes refers to fine jade.
      </p>
