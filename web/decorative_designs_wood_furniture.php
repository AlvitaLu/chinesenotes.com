      <h3 class='article'>
        <a name="furniture"></a>
        Furniture
        <a href='/word_detail.php?id=873' onmouseover="showToolTip(this, 'jiājù​', 'furniture')" onmouseout="hideToolTip()">家具</a>
      </h3>
      <p>
        Ancient Chinese people <span class='bookTitle'>sat on mats on the floor</span> <a href='/word_detail.php?id=26317' onmouseover="showToolTip(this, '蓆地而坐 xí dì ér zuò', 'to sit on the floor')" 
        onmouseout="hideToolTip()">席地而坐</a>, according to an old saying.
        The earliest furniture was made with reeds and bamboo and was short to be in keeping with this mode of resting.
        It was only later in the Tang that people <span class='bookTitle'>sat with legs hanging down</span> <a href='/word_detail.php?id=26318' onmouseover="showToolTip(this, 'chuí zú ér zuò', 'to sit with legs hanging down / to sit on a high chair')" 
        onmouseout="hideToolTip()">垂足而坐</a> on higher seats.
        The styles now thought of as classical Chinese emerged in the Tang, including beds, tables, seats, closets, and 
        clothes racks.
        Every effort was made to add decorative patterns and fine carvings, giving the artifacts distinctively Chinese 
        characteristics.
        The three main styles of classical Chinese furniture are <span class='bookTitle'>Suzhou style</span> <a href='/word_detail.php?id=26234' onmouseover="showToolTip(this, '蘇式 Sū shì', 'Suzhou style')" 
        onmouseout="hideToolTip()">苏式</a>, <span class='bookTitle'>Guangzhou style</span> <a href='/word_detail.php?id=26235' onmouseover="showToolTip(this, '廣式 Guǎng shì', 'Guangzhou style')" 
        onmouseout='hideToolTip()'>广式</a>, and <span class='bookTitle'>Beijing style</span> <a href='/word_detail.php?id=26236' onmouseover="showToolTip(this, 'Jīng shì', 'Beijing style')" 
        onmouseout="hideToolTip()">京式</a>, in addition to some other regional styles.
      </p>
