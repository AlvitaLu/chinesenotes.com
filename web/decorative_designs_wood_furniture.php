      <p>
        The gardens of Suzhou are beautiful and some of the best places to see classical Chinese furniture.
        Perhaps the best known is the <span class='bookTitle'>Humble Administrator's Garden</span> <a href='/word_detail.php?id=25651' onmouseover="showToolTip(this, '拙政園 Zhuōzhèng Yuán', 'Humble Administrator\'s Garden')" 
        onmouseout='hideToolTip()'>拙政园</a>.
        In the Ming and Qing there were over two hundred gardens in Suzhou.
        Four have now been listed as part of the Suzhou Gardens UNESCO World Heritage Site.
        These gardens had a big influence on the styles of furniture produced in Suzhou.
      </p>
