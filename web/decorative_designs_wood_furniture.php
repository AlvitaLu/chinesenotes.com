        Yellow rosewood was the first choice of timber for furniture because of its hardness, color, and grain.
        Other preferred varieties of wood for furniture were red sandalwood and mahogany, followed by beech.
        Furniture made from softer wood has not survived.
        Table shown above and many of the others on this parge are rosewood.
      </p>
