      <p>
        The Jiangsu area and nearby was an early center of furniture production in the Ming, 
        especially Suzhou, Yangzhou, and Songjiang.
        In particular, Suzhou, was an economic center at this time, with manufacturing of woven silk, embroidery, printing, 
        lacquer ware, and furniture.
        Furniture was produced for the Imperial Court, regional government, and for private residences.
      </p>
