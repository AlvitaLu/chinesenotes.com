      </h3>
      <p>
        The well known blue and white porcelain <a href='/word_detail.php?id=25581' onmouseover="showToolTip(this, 'qīnghuā', 'blue and white (porcelain)')" 
        onmouseout="hideToolTip()">青花</a> was developed at Jingdezhen Kiln in the Yuan Dynasty (1279&mdash;1368).
        The blue color comes from use of an oxide with a high proportion of cobolt. 
      </p>
      <p>
        The pot below was created with a <span class='bookTitle'>greenish-glazed white</span> <a href='/word_detail.php?id=25609' onmouseover="showToolTip(this, 'qīng bái yòu', 'Greenish-glazed white')" 
        onmouseout='hideToolTip()'>青白釉</a> technique, developed at the <span class='bookTitle'>Jingdezhen Kiln</span>,
        where the piece is from.
        The characteristic of this technique is the intermediate color of the glaze between green and white.
        The design is a <span class='bookTitle'>string bead design</span> <a href='/word_detail.php?id=25610' onmouseover="showToolTip(this, '串珠紋 chuàn zhū wén', 'string bead design')" 
