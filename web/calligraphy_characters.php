      <h2 class='article'>
        <a name="writing"></a>
        <a href='/word_detail.php?id=894' onmouseover="showToolTip(this, 'zì', 'letter / symbol / character')" onmouseout="hideToolTip()">字</a>
        Characters
      </h2>
      <p>
        When combining strokes together to form characters there are a few considerations.
        When starting out it can be useful to use 
        <a href='/word_detail.php?id=17224' onmouseover="showToolTip(this, 'zìtiè', 'copybook')" onmouseout="hideToolTip()">字帖</a>
        'copybook' to trace the characters.  Moving on from that it can be useful to use
        paper with printed squared and lightly printed diagonal lines in a 
        <a href='/word_detail.php?id=3826' onmouseover="showToolTip(this, 'mǐ', 'rice')" onmouseout='hideToolTip()'>米</a>
        rice pattern. The <a href='/word_detail.php?id=581' onmouseover="showToolTip(this, 'xià', 'bottom')" onmouseout='hideToolTip()'>下</a>
        character below illustrates this.
      </p>
      <div class="picture">
        <a href='illustrations_use.php?mediumResolution=xia_mi300.png'><img class='use' 
           src='images/xia_mi300.png' alt='下字（颜体） Xia Character (Yan Style)' 
           title='下字（颜体） Xia Character (Yan Style)'/></a>
        <div>
         <a href='/word_detail.php?id=580' onmouseover="showToolTip(this, 'xià', 'next')" onmouseout="hideToolTip()">下</a><a href='/word_detail.php?id=894' onmouseover="showToolTip(this, 'zì', 'letter / symbol / character')" onmouseout="hideToolTip()">字</a>（<a href='/word_detail.php?id=17226' onmouseover="showToolTip(this, 'Yán tǐ', 'calligraphic style of Yan Zhenqing')" onmouseout="hideToolTip()">颜体</a>）
         Xia Character (Yan Style)
        </div>
      </div>
      <p>
        The balance of the character is an important consideration.  It is ok to be heavier 
        on the top or heavier on the bottom characters although they should be balanced
        geometrically.  However, charactes should to be balanced from left to right in terms of 
        weight.  
        Because most characters are not symmetric this means that the main stem will not be 
        centered or the strokes will be longer or thicker on one side.  Imagine if the ink was
        made of iron then the center of the square that the character is set in must also be the 
        center or gravity of the character.  Notice in the 
        <a href='/word_detail.php?id=581' onmouseover="showToolTip(this, 'xià', 'bottom')" onmouseout='hideToolTip()'>下</a>
        character above the main stem is to the left of center.  This is to balance 
        the <a href='/word_detail.php?id=17340' onmouseover="showToolTip(this, 'yòu xià diǎn', 'lower right dot')" onmouseout='hideToolTip()'>右下点</a>
        lower right dot, which would otherwise make the right side heavier.
        However, we can see that the distance from the middle of the character to the top is 
        about the same as from the middle to the bottom.
        In the <a href='/word_detail.php?id=2681' onmouseover="showToolTip(this, 'zhǔ', 'owner / host')" onmouseout="hideToolTip()">主</a><a href='/word_detail.php?id=894' onmouseover="showToolTip(this, 'zì', 'letter / symbol / character')" onmouseout="hideToolTip()">字</a>（<a href='/word_detail.php?id=17226' onmouseover="showToolTip(this, 'Yán tǐ', 'calligraphic style of Yan Zhenqing')" onmouseout="hideToolTip()">颜体</a>）
        Zhu character below the middle horizontal stroke is slightly below the center of the 
        square to make the character balanced top to bottom.
      </p>
      <div class="picture">
        <a href='illustrations_use.php?mediumResolution=zhu_mi300.png'><img class='use' 
           src='images/zhu_mi300.png' alt='主字（颜体） Zhu Character (Yan Style)' 
           title='主字（颜体） Zhu Character (Yan Style)'/></a>
        <div>
         <a href='/word_detail.php?id=2681' onmouseover="showToolTip(this, 'zhǔ', 'owner / host')" onmouseout="hideToolTip()">主</a><a href='/word_detail.php?id=894' onmouseover="showToolTip(this, 'zì', 'letter / symbol / character')" onmouseout="hideToolTip()">字</a>（<a href='/word_detail.php?id=17226' onmouseover="showToolTip(this, 'Yán tǐ', 'calligraphic style of Yan Zhenqing')" onmouseout="hideToolTip()">颜体</a>）
         Zhu Character (Yanti Style)
        </div>
      </div>
      <p>
        The thickness of the strokes is also adjusted to balance the character within the 
        space it occupies.  In general, strokes on the left side are thinner, strokes to the
        right are thicker.  Long strokes are thinner, short strokes are thicker.
        Horizontal strokes are thinner, vertical strokes are thicker.
        downwards-left curved strokes 
        <a href='/word_detail.php?id=2584' onmouseover="showToolTip(this, 'piě', 'downwards-left curved character stroke')" onmouseout='hideToolTip()'>撇</a>
        are thinner and right concave strokes
        <a href='/word_detail.php?id=2585' onmouseover="showToolTip(this, 'nà', 'downwards-right concave stroke')" onmouseout='hideToolTip()'>捺</a>
        are thicker.
        In the the <a href='/word_detail.php?id=2681' onmouseover="showToolTip(this, 'zhǔ', 'owner / host')" onmouseout="hideToolTip()">主</a><a href='/word_detail.php?id=894' onmouseover="showToolTip(this, 'zì', 'letter / symbol / character')" onmouseout="hideToolTip()">字</a>（<a href='/word_detail.php?id=17226' onmouseover="showToolTip(this, 'Yán tǐ', 'calligraphic style of Yan Zhenqing')" onmouseout="hideToolTip()">颜体</a>）
        Zhu character above the horizontal strokes are thinner than the vertical stroke.
        In the <a href='/word_detail.php?id=2614' onmouseover="showToolTip(this, 'niú', 'ox / cow / bull')" onmouseout='hideToolTip()'>牛</a>
        character below the horizontal strokes are also thinner than the vertical stroke.
      </p>
      <div class="picture">
        <a href='illustrations_use.php?mediumResolution=niu_mi300.png'><img class='use' 
           src='images/niu_mi300.png' alt='牛字（颜体） Niu Character (Yan Style)' 
           title='牛字（颜体） Niu Character (Yan Style)'/></a>
        <div>
         <a href='/word_detail.php?id=2614' onmouseover="showToolTip(this, 'niú', 'ox / cow / bull')" onmouseout="hideToolTip()">牛</a><a href='/word_detail.php?id=894' onmouseover="showToolTip(this, 'zì', 'letter / symbol / character')" onmouseout="hideToolTip()">字</a>（<a href='/word_detail.php?id=17226' onmouseover="showToolTip(this, 'Yán tǐ', 'calligraphic style of Yan Zhenqing')" onmouseout="hideToolTip()">颜体</a>）
         Niu Character (Yan Style)
        </div>
      </div>
      <p>
        In Yan style the horizontal lines tend to be thinner than the vertical lines, 
        and this difference is exaggerated compared with other regular script styles.
        In the 
        <a href='/word_detail.php?id=2836' onmouseover="showToolTip(this, 'nèi', 'inside / interior')" onmouseout="hideToolTip()">内</a>
        character below the the right vertical is thicker than the left vertical stroke and the
        top horizontal line.
      </p>
      <div class="picture">
        <a href='illustrations_use.php?mediumResolution=nei_yanti300.png'><img class='use' 
           src='images/nei_yanti300.png' alt='内字（颜体） Nei Character (Yan Style)' 
           title='内字（颜体） Nei Character (Yan Style)'/></a>
        <div>
         <a href='/word_detail.php?id=2836' onmouseover="showToolTip(this, 'nèi', 'inside / interior')" onmouseout="hideToolTip()">内</a>
         Nei Character (Yan Style)
        </div>
      </div>
      <p>
        The length of strokes needs to be adjusted to meet the needs of the individual 
        characters so that the character as a whole is well laid out.  
        For example, in the <a href='/word_detail.php?id=2614' onmouseover="showToolTip(this, 'niú', 'ox / cow / bull')" onmouseout='hideToolTip()'>牛</a>
        character above the top most horizontal stroke is shorter on the left side to accomodate
        the <a href='/word_detail.php?id=17949' onmouseover="showToolTip(this, 'duǎn piě', 'short downwards left curved stroke')" onmouseout="hideToolTip()">短撇</a>
        short downwards left curved stroke.  The lower horizontal stroke is longer on the left side
        because the vertical stroke is to the right of center.
        In the <a href='/word_detail.php?id=304' onmouseover="showToolTip(this, 'sì', 'four')" onmouseout="hideToolTip()">四</a><a href='/word_detail.php?id=894' onmouseover="showToolTip(this, 'zì', 'letter / symbol / character')" onmouseout="hideToolTip()">字</a>（<a href='/word_detail.php?id=17226' onmouseover="showToolTip(this, 'Yán tǐ', 'calligraphic style of Yan Zhenqing')" onmouseout="hideToolTip()">颜体</a>）
        si character below the two interior strokes are not formally named as fundamental strokes 
        because the are simple and do not occur frequently.  Nevertheless,
        they are needed for this character.  
      </p>
      <div class="picture">
        <a href='illustrations_use.php?mediumResolution=si_mi300.png'><img class='use' 
           src='images/si_mi300.png' alt='四字（颜体） Si Character (Yan Style)' 
           title='四字（颜体） Si Character (Yan Style)'/></a>
        <div>
         <a href='/word_detail.php?id=304' onmouseover="showToolTip(this, 'sì', 'four')" onmouseout="hideToolTip()">四</a><a href='/word_detail.php?id=894' onmouseover="showToolTip(this, 'zì', 'letter / symbol / character')" onmouseout="hideToolTip()">字</a>（<a href='/word_detail.php?id=17226' onmouseover="showToolTip(this, 'Yán tǐ', 'calligraphic style of Yan Zhenqing')" onmouseout="hideToolTip()">颜体</a>）
         Si Character (Yan Style)
        </div>
      </div>
      <p>
        Stroke order is an additional consideration.  In general the stroke order is the 
        same as writing with a pen but there are some exceptions.  Left before right, top
        before bottom, inside before outside but finish the inside before closing the gate.
        In the <a href='/word_detail.php?id=304' onmouseover="showToolTip(this, 'sì', 'four')" onmouseout="hideToolTip()">四</a><a href='/word_detail.php?id=894' onmouseover="showToolTip(this, 'zì', 'letter / symbol / character')" onmouseout="hideToolTip()">字</a>（<a href='/word_detail.php?id=17226' onmouseover="showToolTip(this, 'Yán tǐ', 'calligraphic style of Yan Zhenqing')" onmouseout="hideToolTip()">颜体</a>）
        si character above the two interior strokes must be drawn before completing the character
        with the lower horizontal stroke. 
      </p>
      <p>
        Strokes are connected to each other like wooden joints in carpentry (without nails).
        Strokes should be next to each other and mostly touching but distinct.
      </p>
      <p>
        The forms of characters that we use in calligraphy sometimes match the forms that 
        specific calligraphers used rather than the modern equivalent.  For example the
        character <a href='/word_detail.php?id=5361' onmouseover="showToolTip(this, 'píng', 'flat / level / equal')" onmouseout="hideToolTip()">平</a>
        ping in Yan style is often drawn as shown below, which is a form that was common during
        the Tang Dynasty at the time of Yan Zhenqing.
      </p>
