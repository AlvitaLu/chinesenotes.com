      <p>
        Characters showing a top-middle-bottom structure are invariably taller than they are wide.
        The top, the middle, or the bottom part may be larger in accordance with the complexity of the radical and the requirements
        of the individual character.  The middle part of the character will often be compressed.
        Examples of two characters with a left-middle-right structure are shown below.
      </p>
