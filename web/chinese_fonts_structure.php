      <p>
        Upper semi-enclosing radicals like <a href='/word_detail.php?id=1565' onmouseover="showToolTip(this, '戶 hù​', 'a measure word for companies, households, door, families, etc')" onmouseout="hideToolTip()">户</a>、<a href='/word_detail.php?id=1626' onmouseover="showToolTip(this, '廣 guǎng', 'wide / numerous')" onmouseout="hideToolTip()">广</a>、<a href='/word_detail.php?id=6060' onmouseover="showToolTip(this, '廠 chǎng', 'factory / yard / depot / workhouse / works / cliff / slope')" 
