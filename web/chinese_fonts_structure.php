      	Character Structure
      </h3>
      <p>
        The right balance and stability in characters is very important to their appearance.
        Different character structures include characters composed of a single radical <a href='/word_detail.php?id=20273' onmouseover="showToolTip(this, '獨體字 dú tǐ zì​', 'characters composed of a single radical')" 
        onmouseout="hideToolTip()">独体字</a>, with left and right parts <a href='/word_detail.php?id=17526' onmouseover="showToolTip(this, 'zuǒyòu', 'the left and right / retainers and officials')" 
        onmouseout='hideToolTip()'>左右</a>, with top and bottom parts <a href='/word_detail.php?id=9341' onmouseover="showToolTip(this, 'shàngxià', 'up and down / top and bottom')" 
        onmouseout="hideToolTip()">上下</a>, semi-enclosed <a href='/word_detail.php?id=1279' onmouseover="showToolTip(this, 'bàn', 'half')" onmouseout="hideToolTip()">半</a><a href='/word_detail.php?id=11547' onmouseover="showToolTip(this, '包圍 bāowéi', 'to surround / to encircle')" 
        onmouseout="hideToolTip()">包围</a>, and completely enclosed <a href='/word_detail.php?id=1096' onmouseover="showToolTip(this, 'quán', 'all / whole / entire / every / complete')" onmouseout="hideToolTip()">全</a><a href='/word_detail.php?id=11547' onmouseover="showToolTip(this, '包圍 bāowéi', 'to surround / to encircle')" 
        onmouseout="hideToolTip()">包围</a>. Several characters composed of a single radical are shown below.
      </p>
