      <p>
        In the Ming, the production of jade artifacts increased greatly along with the booming hand craft industry 
        and related commercial trade.
        Accordingly, jade artifacts became more popular.
        Taoism was popularized.
        Designs with symbols of luck and prosperity were most common.
      </p>
