        Some of the bells in palaces, temples, and towers are huge.
        The Yongle bell, shown above, is 6.75 meters high and weighs 4.6 tonnes. 
        The bell is decorated with Buddhist <span class='bookTitle'>matras</span> <a href='/word_detail.php?id=5107' onmouseover="showToolTip(this, 'zhòu', 'mantra')" 
        onmouseout="hideToolTip()">咒</a> in both Chinese and Sanskrit.
        The top of the bell is decorated with a fierce looking dragon, indicating that is exclusively for imperial use.
        The circumference at the base is decorated with the traditional Chinese 
        <span class='bookTitle'>eight divinatory trigrams</span> <a href='/word_detail.php?id=18794' onmouseover="showToolTip(this, 'bā​ guà', 'the eight divinatory trigrams')" 
