      <p>
        Statues of lions, usually placed in pairs on either side of an entrance, are a common site outside buildings in China.
        This is connected with their symbolism in Buddhism as being a defender of the Dharma.
        The copper lion statue above at the Lama Temple in Beijing is especially impressive.
        Besides the main sculpture the patterns on the base are intereting.
      </p>
