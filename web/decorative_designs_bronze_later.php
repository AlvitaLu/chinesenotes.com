      <h3 class='article'>
        <a name="later"></a>
        Later Periods
        <a href='/word_detail.php?id=9019' onmouseover="showToolTip(this, '後期 hòuqī', 'late stage / later period')" 
        onmouseout="hideToolTip()">后期</a>
      </h3>
      <p>
        In later periods bronze lost its place as the centerpiece of imperial power.
        Bronze became more affordable to people outside sovereigns and high nobles.
        Also, other materials, such as iron and later steel, were developed that are lighter and stronger than bronze.
        However, bronze retained its place as a material of choice for certain artifacts, such as large bells and incense burners.
      </p>
