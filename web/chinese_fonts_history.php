      <h4>
        <a name="jiaguwen"></a>
        <a href='/word_detail.php?id=2595' onmouseover="showToolTip(this, 'jiǎgǔwén', 'oracle bone script')" 
        onmouseout="hideToolTip()">甲骨文</a>
        Oracle Bone Script
      </h4>
      <p>
        Oracle bone script or Jiaguwen <a href='/word_detail.php?id=2595' onmouseover="showToolTip(this, 'jiǎgǔwén', 'oracle bone script')" 
        onmouseout="hideToolTip()">甲骨文</a> is formed from inscriptions on bones and tortoise shells from the Shang Dyansty
        (c. 1,700&mdash;1,045 BCE).  They are often associated with worship of gods and ghosts but were also used for other, more
        general purposes as well.  They were rediscovered in 1899 by Qing scholar Wang Yirong mixed up with Chinese medicine.
        They had come from Anyang in Henan province.  This was the Yinxu site of the ruins of the Shang Dynasty capital.  
        Today, over 150,000 fragments of Oracle bone inscriptions have been found at the site.  4,500 different characters have
        been found and, of those, more than 1,500 have been interpreted.  
        The characters had very straight and thin lines, reflecting the method of inscription, which was carving with a knife.
        Tortoises are important in Chinese legends and that may explain the prefernce for using tortoise shells.
        Oracle script formed a relatively complete writing system.
        Divinations could include people's names, places, reasons, results, and verification of the divination
        Oracle bone script continued to be used in the Western Zhou (c. 1,045&mdash;771 BCE).
        Oracle bone script is the most important source of historic information about the Shang and Western Zhou dynasties.
      </p>
