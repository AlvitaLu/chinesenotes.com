        Pottery
      </h4>
      <p>
        Geometric symbols on pottery somewhat resembling later Chinese characters have been discovered at sites of the Dawenkou Culture
        <a href='/word_detail.php?id=23649' onmouseover="showToolTip(this, 'Dàsènkǒu Wénhuà', 'Dawenkou Culture')" 
        onmouseout="hideToolTip()">大汶口文化</a> (4,100 BC&mdash;2,600 BCE) in modern day Shandong province.
        Pottery markings were also discovered at the Bronze Age Erlitou Culture <a href='/word_detail.php?id=23650' onmouseover="showToolTip(this, '二里頭 Èrlǐtou', 'Erlitou')" onmouseout="hideToolTip()">二里头</a><a href='/word_detail.php?id=1412' onmouseover="showToolTip(this, 'wénhuà', 'culture')" 
        onmouseout="hideToolTip()">文化</a> (2000 BCE &mdash; 1500 BCE) site in Henan believed to be the the remains of the Xia Dynasty.
        The symbols at Erlitou resemble Oracle bone script <a href='/word_detail.php?id=2595' onmouseover="showToolTip(this, 'jiǎgǔwén', 'oracle bone script')" 
        onmouseout="hideToolTip()">甲骨文</a> from the Shang and Zhou dynasties.  The meanings of these pottery markings are not
        known but they may have either represented simple objects or names of clans.
      </p>
