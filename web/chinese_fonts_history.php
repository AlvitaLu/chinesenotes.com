      <h4>
        <a name="jinwen"></a>
        <a href='/word_detail.php?id=24542' onmouseover="showToolTip(this, 'jīn wén', 'bronze script')" 
        onmouseout="hideToolTip()">金文</a>
        Bronze Script
      </h4>
      <p>
        China entered the Bronze Age in about 3,000 BCE, approximately 500 years after Southwest Asia (the Fertile Crescent).
        Bronzeware in China during the Shang and Zhou dynasties was a high form of art.  A script called Jinwen or Bronze Script
        was developed for use in bronzeware.  In particular, tripod cauldrons (ding) and bells were known for their inscriptions.
        The cauldrons were used more frequently for ceremonies than practical uses and often had text recording important events.
        The bronzeware artifacts were important possessions and often buried with important people.
        Bronze script is similar to oracle bone script but simplified and written more neatly. 
        This reflects the tools used to write the script: a brush was used to draw the character and then they were inscribed into
        the bronze.
        More than 3,000 Jinwen characters have been found and more than 2,000 have been decoded.
        The longest inscription of the Western Zhou is 497 characters on the Maogong cauldron.
      </p>
