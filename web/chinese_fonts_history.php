      	History
      </h3>
      <p>
        From historical investigation, Chinese characters have an approximately 5,000 year history 
        [<a href='chinese_fonts_ref.php'>Han Jianting 2008</a>].
        Rock paintings and patterns on clay pottery date back to the Neolithic Age in China and it may be that these
        are the origin of Chinese characters, becoming stylized and more abstract over time.
        One of the very earliest Chinese writing discovered, called Oracle bone script <a href='/word_detail.php?id=2595' onmouseover="showToolTip(this, 'jiǎgǔwén', 'oracle bone script')" 
        onmouseout="hideToolTip()">甲骨文</a>, was written on bone and turtle shell for the 
        purpose fortune telling.  
        These date back to about 1,300 BCE.
        A similar form of writing called bronze script <a href='/word_detail.php?id=24542' onmouseover="showToolTip(this, 'jīn wén', 'bronze script')" 
        onmouseout="hideToolTip()">金文</a> was used on swords and other metal objects some time later.
        Chinese characters appeared later than Sumerian Cuniform and Egyptian hieroglyphs
        but Chinese is the oldest written language still in use today.
      </p>
      <h4>
        <a name="pottery"></a>
