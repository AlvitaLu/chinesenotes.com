      <h4>
        <a name="smallseal"></a>
        <a href='/word_detail.php?id=2600' onmouseover="showToolTip(this, 'xiǎozhuàn', 'small seal script')" 
        onmouseout="hideToolTip()">小篆</a>
        Lesser Seal Script
      </h4>
      <p>
        Lesser seal script, small seal, or Xiaozhuan was the official script of the Qin Dynasty (221&mdash;206 BCE) defined in the 
        period of standardization of characters.  
        This was the first large scale standardization of Chinese characters, which unified the different variant forms that
        had emerged during the Warring States Period (475&mdash;221 BCE).
        The other forms during the Warring States Period are collectively called large seal script <a href='/word_detail.php?id=2601' onmouseover="showToolTip(this, 'dàzhuàn', 'great seal script')" 
        onmouseout="hideToolTip()">大篆</a>.
        The style was futher simplified from large seal script and became more rounded.
        More characters indicating sound and pictophonetic (including both a pictographic meaning and a sound) emerged.
        At this stage the pictographic meanings had mostly been abstracted to the point were it required a large amount of interpretation
        to deduce the meaning of the pictographic elements.
        All imperial inscriptions during this dynasty and frequently in later dynasties were written in lesser seal script.
        Lesser seal script has been used until modern times for offical seals and stamps.
      </p>
      <h4>
        <a name="lishu"></a>
        <a href='/word_detail.php?id=2597' onmouseover="showToolTip(this, '隸書 lì​shū​', 'clerical script')" 
        onmouseout="hideToolTip()">隶书</a>
        Clerical Script
      </h4>
      <p>
        In the Qin Dyansty writing began to be used on a large scale.  Officials in lower levels of Qin court used bamboo
        and wood strips to record events and other texts.  
        The style that they used in writing quickly on the bamboo and wood strips was simplified and different in form from
        lesser seal that came to be known as clerical or official script <a href='/word_detail.php?id=2597' onmouseover="showToolTip(this, '隸書 lì​shū​', 'clerical script')" 
        onmouseout="hideToolTip()">隶书</a>.
        In order to draw the characters more quickly many of the curves in lesser seal were changed to straight lines.
        Clerical script is very similar to the regular script of modern Chinese.
        This was a large jump from mainly pictographic characters with some abstractions to a character set that was highly 
        abstracted and nearly unrecognizable from the original pictographs that they were originally derived from.
        Characters serving as components in other lesser seal script characters like <a href='/word_detail.php?id=1609' onmouseover="showToolTip(this, 'shuǐ', 'water / river')" 
        onmouseout="hideToolTip()">水</a> (water), <a href='/word_detail.php?id=922' onmouseover="showToolTip(this, 'shǒu', 'hand')" 
        onmouseout="hideToolTip()">手</a> (hand), and <a href='/word_detail.php?id=1849' onmouseover="showToolTip(this, 'xīn', 'heart')" 
        onmouseout="hideToolTip()">心</a> (heart) were simplified and became the radicals <a href='/word_detail.php?id=6870' onmouseover="showToolTip(this, 'shuǐ', 'Kangxi radical 85')" onmouseout="hideToolTip()">氵</a>、<a href='/word_detail.php?id=6834' onmouseover="showToolTip(this, 'shǒu', 'Kangxi radical 64')" onmouseout="hideToolTip()">扌</a>、and <a href='/word_detail.php?id=6827' onmouseover="showToolTip(this, 'xīn', 'Kangxi radical 61')" 
        onmouseout="hideToolTip()">忄</a> in modern Chinese.
      </p>
      <p>
        Clerical script was further developed after the Qin Dynasty in the Han Dyansty.
        The style used in the Han is called Han Li <a href='/word_detail.php?id=25229' onmouseover="showToolTip(this, '漢隸 Hàn lì', 'Han style clerical script')" 
        onmouseout="hideToolTip()">汉隶</a>.
        About 30,000 bamboo and wood strips with clerical script were excavated from the Han Dynasty Juyan site alone.
        Paper was invented in the Western Han Dyansty (206 BCE&mdash;25 CE) but not widely used until the Eastern Han Dynasty 
        (25&mdash220 CE).
      </p>
      <h4>
        <a name="kaishu"></a>
        <a href='/word_detail.php?id=2599' onmouseover="showToolTip(this, '楷書 kǎishū​', 'regular script')" 
        onmouseout="hideToolTip()">楷书</a>
        Regular Script
      </h4>
      <p>
        The most common script in use today is regular script <a href='/word_detail.php?id=2599' onmouseover="showToolTip(this, '楷書 kǎishū​', 'regular script')" 
        onmouseout="hideToolTip()">楷书</a>.
        It is easy to read and create printed fonts for.
        Regular script evolved near the end of the Han Dyansty from clerical script and matured in the Sui (581&mdash;618) 
        and Tang (618&mdash;907) dynasties.
        Compared to clerical script it is more square and regular and the strokes are more straight than curved.
        The hook emerged as a characteristic of certain strokes.
      </p>
