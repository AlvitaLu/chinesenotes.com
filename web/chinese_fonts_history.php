        <a href='/word_detail.php?id=25252' onmouseover="showToolTip(this, 'Rìwén', 'Japanese language')" 
        onmouseout="hideToolTip()">日文</a>
        Japanese
      </h4>
      <p>
        Japanese characters originated in China beginning in the Tang.  Japanese characters in many cases from Chinese characters
        because of the variants that have been retained, the calligraphic styles that have been standardized on, and the 
        simplifications made.
        Japanese simplified characters are called shinjitai <a href='/word_detail.php?id=25251' onmouseover="showToolTip(this, '新字體 xīn zìtǐ', 'shinjitai / Japanese simplified characters')" 
        onmouseout="hideToolTip()">新字体</a>.
        The characters overlap but with many differences.  
        Many of the variant characters in Japanese are obsolete or considered incorrect in Chinese although they can be understood.
        From the pronounciation of many words in Japanese it is apparent that the pronounciation as well as the written form was 
        borrowed. Examples of characters that are similiar but different to Chinese characters are <a href='/character_search.php?unicode=30330' onmouseover="showToolTip(this, 'fā fà 发 發', 'to issue / to dispatch / to send out / to emit')" 
        onmouseout="hideToolTip()">発</a> (to send out), which is equivalent to the simplified Chinese character <a href='/character_search.php?unicode=21457' onmouseover="showToolTip(this, 'fā fà fǎ bō 発 發 髮', 'an issue / to dispatch / to send out / hair')" 
        onmouseout="hideToolTip()">发</a> and traditional <a href='/character_search.php?unicode=30332' onmouseover="showToolTip(this, 'fā fà bō 发 発', 'to issue / to dispatch / to send out / to emit')" 
        onmouseout="hideToolTip()">發</a>.
      </p>
      <p>
        For more on the history of Chinese characters see the book <span class='bookTitle'>Chinese Characters</span>
        [<a href='chinese_fonts_ref.php'>Han Jianting 2008</a>].
      </p>
