        Many calligraphic works in cursive script are highly valued but are very difficult to read.
        It is entirely based on regular script.
        However, some simplified variants of regular characters have been inspired from cursive script.
      </p>
      <h4>
        <a name="jianhuazi"></a>
        <a href='/word_detail.php?id=25247' onmouseover="showToolTip(this, '簡化字 jiǎnhuà zì', 'simplified Chinese characters')" 
        onmouseout="hideToolTip()">简化字</a>
        Simplified Characters
      </h4>
      <p>
        Chinese characters were simplified one by one periodically throughout Chinese history.
        However, a major initiative to simplify characters was carried out after the founding of the People's Republic
        in 1949 to address the problems with the low literacy rate in China at the time.
        The resulting set of characters differs from the traditional characters <a href='/word_detail.php?id=14577' onmouseover="showToolTip(this, '繁體字 fántǐzì​', 'traditional Chinese characters')" 
        onmouseout='hideToolTip()'>繁体字</a> still in use in Hong Kong and Taiwan and in many Chinese language schools and 
        departments outside China.
        Simplified characters were announced in 1964 in the Complete List of Simplified Characters <a href='/word_detail.php?id=25250' onmouseover="showToolTip(this, '簡化字總表 Jiǎnhuà Zì Zǒng Biǎo', 'Complete List of Simplified Characters')" 
        onmouseout='hideToolTip()'>简化字总表</a> national standard and revised in 1986.
        There are a total of 2,235 simplified characters replacing 2,263 traditional characters.
      </p>
      <p>
        In most cases there is an unambiguous relation between the traditional and the equivalent simplified character.
        However, there are some exceptions.  
        For example, the simplified character <a href='/word_detail.php?id=821' onmouseover="showToolTip(this, '臺 tái', 'measure word for electrical appliances, such as computers, televisions, etc')" 
        onmouseout="hideToolTip()">台</a> may be any of the traditional characters <a href='/word_detail.php?id=821' onmouseover="showToolTip(this, '臺 tái', 'measure word for electrical appliances, such as computers, televisions, etc')" o
        nmouseout="hideToolTip()">台</a>（台灣 Taiwan）、臺（平臺 channel）、檯（櫃檯 sales counter）、or 颱（颱風 taiphoon）.
        There are only a handful of cases like this.
      </p>
      <h4 class='article'>
        <a name="variants"></a>
        <a href='/word_detail.php?id=24598' onmouseover="showToolTip(this, '異體字 yìtǐzì', 'variant Chinese character')" 
        onmouseout="hideToolTip()">异体字</a>
        Variants
      </h4>
      <p>
        Amazingly, characters that were in use over 2,000 years ago are still part of the Chinese language today.
        Not surprisingly, however, some of the characters changed in form over the years.  These are called variants.
        A variant is conceptually the same character, with the same meaning but is written differently.
        A variant of the character ming <a href='/word_detail.php?id=3062' onmouseover="showToolTip(this, 'míng', 'clear / bright')" 
        onmouseout="hideToolTip()">明</a> used by Tang dynasty calligrapher Yan Zhenqing is shown above.
      </p>
      <p>
        In some cases a character was written a very different way in the past.  For example, this is the way that
        Yan Zhenqing wrote the character bi​ng <a href='/word_detail.php?id=346' onmouseover="showToolTip(this, '並 bì​ng', 'and / furthermore / also')" 
        onmouseout="hideToolTip()">并</a>(traditional 並).  The rendering looks more like the variant <a href='/word_detail.php?id=24596' onmouseover="showToolTip(this, 'bìng', 'also / furthermore')" 
        onmouseout="hideToolTip()">竝</a>.
      </p>
