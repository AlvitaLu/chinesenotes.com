      <h3 class='article'>
        <a name="cursive"></a>
        <a href='/word_detail.php?id=2598' onmouseover="showToolTip(this, '草書 cǎoshū​', 'cursive script')" onmouseout="hideToolTip()">草书</a><a href='/word_detail.php?id=387' onmouseover="showToolTip(this, 'hé', 'and')" onmouseout="hideToolTip()">和</a><a href='/word_detail.php?id=2593' onmouseover="showToolTip(this, '行書 xíngshū​', 'semi-cursive script')" 
        onmouseout="hideToolTip()">行书</a>
        Cursive  and Semi-Cursive Script
      </h3>
      <p>
        One of the early pioneers of Chinese calligraphy was Wang Xizhi <a href='/word_detail.php?id=7808' onmouseover="showToolTip(this, 'Wáng Xīzhī', 'Wang Xizhi')" 
        onmouseout='hideToolTip()'>王羲之</a> (303&mdash;361), who lived in the Eastern Jin.
        He was especially known for his semi-cursive script.
      </p>
      <h3 class='article'>
        <a name="composition"></a>
        <a href='/word_detail.php?id=17284' onmouseover="showToolTip(this, 'zhāngfǎ', 'regulation / organization / composition')" 
        onmouseout="hideToolTip()">章法</a>
        Composition
      </h3>
      <p>
        The most common kinds of traditional compositions include the wall scroll <a href='/word_detail.php?id=24819' onmouseover="showToolTip(this, '條幅 tiáofú', 'a wall scroll')" 
        onmouseout="hideToolTip()">条幅</a>, works for the center of a large hall <a href='/word_detail.php?id=17278' onmouseover="showToolTip(this, 'Zhōngtáng', 'a calligraphic work for the center of a large hall')" 
        onmouseout="hideToolTip()">中堂</a>, couplets <a href='/word_detail.php?id=5924' onmouseover="showToolTip(this, '對聯 duì​lián', 'vertical written couplet')" 
        onmouseout="hideToolTip()">对联</a>, horizontal inscribed board <a href='/word_detail.php?id=9029' onmouseover="showToolTip(this, '匾額 biǎn é', 'a horizontal inscribed board')" 
        onmouseout="hideToolTip()">匾额</a>, a horizontal hanging scroll <a href='/word_detail.php?id=24822' onmouseover="showToolTip(this, '橫披 héngpī', 'a horizontal hanging scroll')" 
        onmouseout="hideToolTip()">横披</a>, album <a href='/word_detail.php?id=24825' onmouseover="showToolTip(this, '冊頁 cè yè', 'an album of calligraphy or paintings')" 
        onmouseout="hideToolTip()">册页</a>, and fan <a href='/word_detail.php?id=24826' onmouseover="showToolTip(this, 'shànmiàn', 'covering of a fan')" 
        onmouseout="hideToolTip()">扇面</a>.  These all can make interesting backgrounds for digital graphic designs featuring 
        calligraphic text.
      </p>
      <p>
        Wall scrolls may come in individually or in a set <a href='/word_detail.php?id=24827' onmouseover="showToolTip(this, '屏條 píngtiáo', 'a set of hanging scrolls')" 
        onmouseout="hideToolTip()">屏条</a>.  The couplet <a href='/word_detail.php?id=5924' onmouseover="showToolTip(this, '對聯 duì​lián', 'vertical written couplet')" 
        onmouseout="hideToolTip()">对联</a> composition originated in the Ming. They can be three characters <a href='/word_detail.php?id=24831' onmouseover="showToolTip(this, 'sān yán', 'three character form of poetry')" 
        onmouseout='hideToolTip()'>三言</a>, five characters <a href='/word_detail.php?id=24832' onmouseover="showToolTip(this, 'wŭ yán', 'five character form of poetry / a five character sentence')" 
        onmouseout='hideToolTip()'>五言</a>, seven characters <a href='/word_detail.php?id=24833' onmouseover="showToolTip(this, 'qī yán', 'seven character form of poetry / a five character sentence')" 
        onmouseout='hideToolTip()'>七言</a>, or longer.  In horizontal scrolls text flows from right to left.  This tradition continues
        today in many modern signs, especially neon signs for shopfronts.  Text describing the origin of a calligraphic work <a href='/word_detail.php?id=24834' onmouseover="showToolTip(this, 'luò kuǎn', 'text describing the origin of a calligraphic work')" 
        onmouseout="hideToolTip()">落款</a>, including calligrapher's name and reason for writing it, is placed at the left.
        Horizontal inscribed board <a href='/word_detail.php?id=9029' onmouseover="showToolTip(this, '匾額 biǎn é', 'a horizontal inscribed board')" 
        onmouseout="hideToolTip()">匾额</a> are usually placed in temples, park pavillions, class rooms, or studios.
        An album <a href='/word_detail.php?id=24825' onmouseover="showToolTip(this, '冊頁 cè yè', 'an album of calligraphy or paintings')" 
        onmouseout='hideToolTip()'>册页</a> style usually folds out into a long horizontal work comparable with a horizontal scroll.
      </p>
      <p>
        Arranging the areas of ink and white space is also important in composition.
        In most cases, the characters will be read vertically and may or may not form a grid depending on the alignment of the
        characters horizontallly.
        Traditionally, calligraphers try to avoid having all the characters the same size and aligned in a grid since it feels
        too stiff.
        This is hard to avoid in computer fonts but the stiffness of Song and Hei fonts is one of the reasons that people turn to
        calligraphic and artistic fonts.
      </p>
