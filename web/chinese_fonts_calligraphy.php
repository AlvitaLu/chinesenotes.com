      <h2 class='article'>
        <a name="calligraphic"></a>
        <a href='/word_detail.php?id=3433' onmouseover="showToolTip(this, '書法 shū​fǎ', 'calligraphy')" onmouseout="hideToolTip()">书法</a><a href='/word_detail.php?id=2525' onmouseover="showToolTip(this, '字體 zì​tǐ', 'calligraphic style / typeface')" 
        onmouseout="hideToolTip()">字体</a>
        Calligraphic Fonts
      </h2>
      <p>
        Historic calligraphy works, especially inscriptions, are a good source for creating fonts.
        They can be traced and converted into graphics and font files.
        Chinese text can be written in several different script styles, including:
      </p>
      <ul>
        <li>
          Oracle bone script <a href='/word_detail.php?id=2595' onmouseover="showToolTip(this, 'jiǎgǔwén', 'oracle bone script')" 
          onmouseout='hideToolTip()'>甲骨文</a>
          <p>
            The earliest form of Chinese writing
          </p>
        </li>
        <li>
          Bronze script <a href='/word_detail.php?id=24542' onmouseover="showToolTip(this, 'jīn wén', 'bronze script')" 
          onmouseout="hideToolTip()">金文</a>
          <p>
            An early form of writing found on caldrons and other Bronze Age implements
          </p>
        </li>
        <li>
          Large Seal script or Great Seal script <a href='/word_detail.php?id=2601' onmouseover="showToolTip(this, 'dàzhuàn', 'great seal script')" 
          onmouseout='hideToolTip()'>大篆</a>
          <p>
            Originated in the Western Zhou (1045—771 BCE).
            This is mostly used in signs, calligraphy, seals, and logos.
          </p>
        </li>
        <li>
          Small seal script <a href='/word_detail.php?id=2600' onmouseover="showToolTip(this, 'xiǎozhuàn', 'small seal script')" 
          onmouseout='hideToolTip()'>小篆</a>
          <p>
            The form of Chinese character standardized by the Qin Dynasty (221—207 BCE).
          </p>
        </li>
        <li>
          Regular script <a href='/word_detail.php?id=2599' onmouseover="showToolTip(this, '楷書 kǎishū​', 'regular script')" 
          onmouseout="hideToolTip()">楷书</a>
          <p>
            This is the most common form of script seen today.  It is the most readable style of script and the basis for all
            commonly used fonts, including Song Hei type faces, intended for composition and display of Chinese text.
            It appeared in the Cao Wei dynasty (220—265).
          </p>
        </li>
        <li>
          Clerical script <a href='/word_detail.php?id=2597' onmouseover="showToolTip(this, '隸書 lì​shū​', 'clerical script')" 
          onmouseout="hideToolTip()">隶书</a>
          <p>
            This is an older style of script similar to regular script but wider and more square with more isolated strokes.
            It is commonly used today for headers, logos, and shop front signs.
          </p>
        </li>
        <li>
          Cursive script <a href='/word_detail.php?id=2598' onmouseover="showToolTip(this, '草書 cǎoshū​', 'cursive script')" 
          onmouseout='hideToolTip()'>草书</a>
          <p>
            Cursive script is difficult to read and mostly used in signs, calligraphy, and logos.
          </p>
        </li>
        <li>
          Semi-cursive script <a href='/word_detail.php?id=2593' onmouseover="showToolTip(this, '行書 xíngshū​', 'semi-cursive script')" 
          onmouseout='hideToolTip()'>行书</a>
          <p>
            A partially cursive style of writing.  It has the flavor of cursive writing but is easier to read.
          </p>
        </li>
      </ul>
      <p>
        Calligraphy students may progress in three stages: tracing or copying, immitating, and creating.
        In the first stage students will trace or copy to master the basic techniques.  
        In the second stage students will imitate good role models and accumulate a repertoire of characters.
        Finally, having mastered the basic techniques and established a sufficient repertoire of characters calligraphers 
        can create their own works in their own unique styles.
        Needing to draw characters that are not in templates or calligraphic dictionaries a calligrapher will need to find
        similar characters with the required radicals.
        For digital art, a similar process can apply: learning basic techniques, immitating to establish a repertoire,
        and then creating one's own unique works.
        In digital art, one can directly copy from one's repertoire.
      </p>
      <h3 class='article'>
        <a name="regular"></a>
        <a href='/word_detail.php?id=2599' onmouseover="showToolTip(this, '楷書 kǎishū​', 'regular script')" 
        onmouseout="hideToolTip()">楷书</a>
        Regular Script
      </h3>
      <p>
        Great calligraphy masters from the Tang and Song dynasties laid the foundation for styles most commonly used today.
        The four great masters of regular script <a href='/word_detail.php?id=17247' onmouseover="showToolTip(this, '楷書四大家 kǎishū​ sì​ dà jiā', 'The four great masters of regular script')" 
        onmouseout="hideToolTip()">楷书四大家</a> are Yan Zhenqing <a href='/word_detail.php?id=7809' onmouseover="showToolTip(this, '顏真卿 Yán Zhēnqīng', 'Yan Zhenqing')" 
        onmouseout="hideToolTip()">颜真卿</a> (709－785), Ouyang Xun <a href='/word_detail.php?id=17222' onmouseover="showToolTip(this, '陽歐詢 Ōuyáng Xún', 'Ouyang Xun')" 
        onmouseout="hideToolTip()">欧阳询</a> (557－641), Liu Gongquan <a href='/word_detail.php?id=17243' onmouseover="showToolTip(this, '柳公權 Liǔ Gōngquán', 'Liu Gongquan')" 
        onmouseout="hideToolTip()">柳公权</a> (778－865), and Zhao Mengfu <a href='/word_detail.php?id=17245' onmouseover="showToolTip(this, '趙孟頫 Zhào Mèngfǔ', 'Zhao Mengfu')" 
        onmouseout="hideToolTip()">赵孟頫</a> (1254－1322).
      </p>
      <h4 class='article'>
        <a name="yanzhenqing"></a>
        <a href='/word_detail.php?id=7809' onmouseover="showToolTip(this, '顏真卿 Yán Zhēnqīng', 'Yan Zhenqing')" 
        onmouseout="hideToolTip()">颜真卿</a>
        Yan Zhenqing
      </h4>
      <p>
        The style  developed by Yan Zhenqing <a href='/word_detail.php?id=7809' onmouseover="showToolTip(this, '顏真卿 Yán Zhēnqīng', 'Yan Zhenqing')" 
        onmouseout="hideToolTip()">颜真卿</a>, called Yan style <a href='/word_detail.php?id=17226' onmouseover="showToolTip(this, '顏體 Yán tǐ', 'calligraphic style of Yan Zhenqing')" 
        onmouseout="hideToolTip()">颜体</a>, is one of the great figures in Chinese calligraphy and is the style learned by most 
        beginning calligraphy students.
        Yan Zhenqing was born in the Tang capital Jingzhao, present day Xi'an in Shaanxi province.
        He came from a family of scholars and a number of members of the Yan clan had worked in the imperial court.
        He passed the highest imperial civil service examination and worked as a Tang court official and imperial tutor.
      </p>
      <p>
        Yan style borrows some elements of clerical and seal script styles, being relatively wide with more isolated strokes.
        It invariable has thin horizontal and thick vertical strokes with rounded endings.
        The curvature of the right-most vertical <a href='/word_detail.php?id=2575' onmouseover="showToolTip(this, '豎 shù​', 'straight vertical character stroke')" 
        onmouseout="hideToolTip()">竖</a> and vertical hooked strokes <a href='/word_detail.php?id=2575' onmouseover="showToolTip(this, '豎 shù​', 'straight vertical character stroke')" 
        onmouseout="hideToolTip()">竖</a> is exaggerated.
        Many of the horizontal strokes are leaning.
        Long horizontal strokes are usually thin in the center and thick at the ends.
        The stroke turns of horizontal and down strokes like ta jian zhe <a href='/word_detail.php?id=24650' onmouseover="showToolTip(this, 'tā jiān zhé', 'ta jian zhe / CJK Stroke HZ')" 
        onmouseout="hideToolTip()">塌肩折</a> are unique.
        In groups of similar strokes Yan style varies thickness and turns in a unique way.
        According to conventional use in the Tang dynasty, Yan Zhenqing uses a number of character variants that are not used today.  
        In addition, in some cases, he uses the same forms as simplified Chinese, where one might expect a traditional variant.
        In fact, simplified Chinese characters, although not standardized until the 1950's and 1960's, uses some variants 
        that were in existence in previous historic times.  This is also the reason that Japanese text has a number of simplified
        characters.
      </p>
      <p>
        Yan Zhenqing was a prolific calligrapher, which is another good reason to study his works &mdash; coverage.  
        His earliest work is Yanta Timing 《<a href='/word_detail.php?id=24678' onmouseover="showToolTip(this, 'Yàntǎ', 'Yanta')" onmouseout="hideToolTip()">雁塔</a><a href='/word_detail.php?id=24679' onmouseover="showToolTip(this, '題名 tí míng', 'to sign one\'s name')" 
        onmouseout="hideToolTip()">题名</a>》 .  His best known work is Duo Bao Ta Gan Ying Bei 《<a href='/word_detail.php?id=400' onmouseover="showToolTip(this, 'duō', 'indicates a number greater than the number preceding it')" onmouseout="hideToolTip()">多</a><a href='/word_detail.php?id=3707' onmouseover="showToolTip(this, '寶 bǎo', 'a jewel / gem / a treasure')" onmouseout="hideToolTip()">宝</a><a href='/word_detail.php?id=3374' onmouseover="showToolTip(this, 'tǎ', 'pagoda / tower')" onmouseout="hideToolTip()">塔</a><a href='/word_detail.php?id=2393' onmouseover="showToolTip(this, '感應 gǎnyì​ng', 'response / reaction / interaction')" onmouseout="hideToolTip()">感应</a><a href='/word_detail.php?id=7796' onmouseover="showToolTip(this, 'bēi', 'a monument / an upright stone tablet / stele')" 
        onmouseout="hideToolTip()">碑</a>》.  A well known work written during his middle years that Yan Zhenqing was proud of 
        is Dongfang Shuo Hua Zan 《<a href='/word_detail.php?id=8149' onmouseover="showToolTip(this, '東方 dōngfāng', 'The East / The Orient')" onmouseout="hideToolTip()">东方</a><a href='/word_detail.php?id=24681' onmouseover="showToolTip(this, 'shuò', 'first day of lunar month / the north')" onmouseout="hideToolTip()">朔</a><a href='/word_detail.php?id=3409' onmouseover="showToolTip(this, '畫 huà', 'picture / painting')" onmouseout="hideToolTip()">画</a><a href='/word_detail.php?id=8680' onmouseover="showToolTip(this, '讚 zàn', 'to praise')" 
        onmouseout="hideToolTip()">赞</a>》.  Two of his works in semi-cursive script are 《<a href='/word_detail.php?id=18074' onmouseover="showToolTip(this, 'jì​', 'to give an offering in a religious ceremony')" onmouseout="hideToolTip()">祭</a><a href='/word_detail.php?id=6578' onmouseover="showToolTip(this, 'zhí', 'nephew')" onmouseout="hideToolTip()">侄</a><a href='/word_detail.php?id=2745' onmouseover="showToolTip(this, 'wéngǎo', 'manuscript / article / draft')" 
        onmouseout="hideToolTip()">文稿</a>》, written in memory of Yan Zhenqing's nephew who was tragically killed,
        and his later work Zheng Zuo Wei Tie 《<a href='/word_detail.php?id=3429' onmouseover="showToolTip(this, '爭 zhēng', 'to struggle / to fight')" onmouseout="hideToolTip()">争</a><a href='/word_detail.php?id=10037' onmouseover="showToolTip(this, 'zuòwèi', 'seat')" onmouseout="hideToolTip()">座位</a><a href='/word_detail.php?id=10100' onmouseover="showToolTip(this, '貼 tiē', 'to stick / to paste / to keep close to / to fit snugly / to subsidize')" 
        onmouseout="hideToolTip()">贴</a>》.
      </p>
      <p>
        Despite Yan Zhenqing's large number of works,
        you will likely find, however, that the number of characters in historic inscriptions is not sufficient to form a complete
        font.  For example, the Yan Family Ceremonial Inscription <a href='/word_detail.php?id=17253' onmouseover="showToolTip(this, '顏勤禮碑 Yán Qín Lǐ Bēi', 'Yan Family Ceremonial Inscription')" 
        onmouseout='hideToolTip()'>颜勤礼碑</a>, 
        a well known inscription from Yan Zhenqing in his later period has around 1667 characters total 
        (with considerable repetition of characters).
        You will have to fill in the remaining characters from the glyphs developed and your knowledge of calligraphy.
        Working with a vector format is important for this because of the ease of transforming and reusing.
        The figure below is traced from a section of of the Yan Family Ceremonial Inscription.
        Note that the text is written top to bottom, right to left.
      </p>
