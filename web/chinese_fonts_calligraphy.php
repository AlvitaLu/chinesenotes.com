      <p>
        Yan style inspired many people and was widely studied.  
        The first well known calligrapher to adopt a Yan style was Liu Gongquan <a href='/word_detail.php?id=17243' onmouseover="showToolTip(this, '柳公權 Liǔ Gōngquán', 'Liu Gongquan')" 
        onmouseout='hideToolTip()'>柳公权</a> (778&mdash;865).  Later Yang Ningshi <a href='/word_detail.php?id=24773' onmouseover="showToolTip(this, '楊凝式 Yáng Níngshì', 'Yang Ningshi')" 
        onmouseout="hideToolTip()">杨凝式</a> (873&mdash;954) adopted a Yan style.
        In the Song Dyansty (960—1279) Yan Zhenqing became a widely recognized model for calligraphy and was very influential on the
        four famous Song calligraphers <a href='/word_detail.php?id=24782' onmouseover="showToolTip(this, 'Sòng sì jiā', 'four famous Song calligraphers')" 
        onmouseout="hideToolTip()">宋四家</a>, Su Shi <a href='/word_detail.php?id=17655' onmouseover="showToolTip(this, '蘇軾 Sū​ Shì​', 'Su Shi')" onmouseout="hideToolTip()">苏轼</a> （Song Dongpo), Huang Tingjian <a href='/word_detail.php?id=24783' onmouseover="showToolTip(this, '黃庭堅 Huáng Tíngjiān', 'Huang Tingjian')" onmouseout="hideToolTip()">黄庭坚</a>, Mi Fei <a href='/word_detail.php?id=24786' onmouseover="showToolTip(this, 'Mǐ Fèi', 'Mi Fei')" onmouseout="hideToolTip()">米芾</a>, and Cai Xiang <a href='/word_detail.php?id=24787' onmouseover="showToolTip(this, 'Cài Xiāng', 'Cai Xiang')" 
        onmouseout="hideToolTip()">蔡襄</a>.  In the Yuan and Ming dynasties Zhao Mengfu <a href='/word_detail.php?id=17245' onmouseover="showToolTip(this, '趙孟頫 Zhào Mèngfǔ', 'Zhao Mengfu')" 
        onmouseout='hideToolTip()'>赵孟頫</a> and Li Dongyang <a href='/word_detail.php?id=24792' onmouseover="showToolTip(this, '李東陽 Lǐ Dōngyáng', 'Li Dongyang')" 
        onmouseout='hideToolTip()'>李东阳</a> were greatly influenced by Yan Zhenqing.  In the Qing Dynasty Sheng Zhuanshuai <a href='/word_detail.php?id=24793' onmouseover="showToolTip(this, '盛轉衰 Shèng Zhuǎnshuāi', 'Sheng Zhuanshuai')" 
        onmouseout='hideToolTip()'>盛转衰</a> was a pioneer in using tracings to analyze the and style and authenticity of Yan Zhenqing
        works.  Liu Yong <a href='/word_detail.php?id=24803' onmouseover="showToolTip(this, '劉墉 Liú Yōng', 'Liu Yong')" 
        onmouseout='hideToolTip()'>刘墉</a>, He Shaoji <a href='/word_detail.php?id=24804' onmouseover="showToolTip(this, '何紹基 Hé Shàojī', 'He Shaoji')" 
        onmouseout='hideToolTip()'>何绍基</a>, and Weng Tonghe <a href='/word_detail.php?id=24806' onmouseover="showToolTip(this, 'Wēng Tónghé', 'Weng Tonghe')" 
        onmouseout="hideToolTip()">翁同龢</a> were influenced by Yan Zhenqing. 
      </p>
