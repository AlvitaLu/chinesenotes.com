      <p>
        From jade artifacts in the shape of silkworms and a wide vocabulary relating to silk production and clothing, it is clear
        that silk was popular in the Xia, Shang, and Zhou dynasties. 
        Production was standardized, labor specialized, and work was supervised by government officials.
        In the Spring and Autumn and the Warring States periods a wider variety of silk artifacts were produced.
        Suzhou was established as the main center of silk production.
        A reproduction of a brocade from the Warring States Period is shown below.
      </p>
