      <p>
        In 1972 a Han dynasty tomb was discovered in the No. 1 <span class='bookTitle'>Mawang Pit</span> <a href='/word_detail.php?id=25852' onmouseover="showToolTip(this, '馬王堆 mǎ Wáng duī', 'Mawang Pit')" 
        onmouseout='hideToolTip()'>马王堆</a>.
        Included in the discoveries were silk artifacts with fine color printed designs.
        The designs included plants with vines, flower buds, and bunches of leaves arranged in intricate patterns.
        More than 20 different designs were found in total.
        The designs were most likely created with openwork printing blocks or from carved relief printing plates.
        The picture below shows printed silk fabric from the Western Han (206 BCE — 25 CE).
      </p>
