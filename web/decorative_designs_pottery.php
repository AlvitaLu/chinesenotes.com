      <p>
        The main technologies relating to pottery that were developed over the Neolithic were firing techniques, paint materials,
        and use of a wheel.
        Early pottery differs from porcelain in the lower firing temperatures used.
        Of course, in the Neolithic there was no way to measure the temperature and early pottery was fired in open fires.
        The main benefit of firing at low temperatures is reducing the amount of moisture in the clay.
        Besides moisture in the pores of the clay, there is also water bound (hydrated) in the complex chemical compounds 
        that form clay.  This water can also be reduced and that also changes the chemical structure of the clay resulting in
        harder clay artifacts.
      </p>
      <p>
        The designs on pottery in the Neolithic were the first large scale use of decorative designs in Chinese history.
        It laid the foundation for the use of decorative patterns in a wide range of media in the coming millenia.
      </p>
