        <a name="pottery"></a>
        Pottery
        <a href='/word_detail.php?id=22975' onmouseover="showToolTip(this, 'táoqì', 'pottery')" onmouseout="hideToolTip()">陶器</a>
      </h2>
      <p>
        The main theme in looking at early pottery is the emergence of Chinese civilization in the late Neolithic.
        The development of pottery is linked to the development of agriculture and establishment of fixed residences.
        Prior to this, hunter-gatherers, who were on the move all the time could not carry such heavy things around.
      </p>
      <p>
        Making pottery was the first human experience in changing a material into a another that could be used for 
        tools and utensils.
        Pottery is different from earlier implements made from stone and bones in that the clay that pottery is made from can
        be molded, dried in air, and then hardened in a fire.
        It is a perfect medium for art, either by sculpture or by painting.
        The earliest Neolithic pottery in China dates to about 8,000 BCE.
        The oldest piece found to date is a jar discovered in Wannian County, Jiangxi Province.
        The jar shown below excavated from <span class='bookTitle'>Xinglongwa</span> <a href='/word_detail.php?id=25486' onmouseover="showToolTip(this, '興隆洼 Xīnglóngwā', 'Xinglongwa')" 
        onmouseout="hideToolTip()">兴隆洼</a> site in Aohanqi, Inner Mongolia is representative of early pottery.
        Simple geometric patterns are apparent on the outside.
      </p>
