        Wood
        <a href='/word_detail.php?id=24380' onmouseover="showToolTip(this, 'mùliào', 'wood / lumber / timber')" onmouseout="hideToolTip()">木料</a>
      </h2>
      <h3 class='article'>
        <a name="background"></a>
        Background
        <a href='/word_detail.php?id=1826' onmouseover="showToolTip(this, 'bèijǐng', 'background')" 
        onmouseout="hideToolTip()">背景</a>
      </h3>
      <p>
        Wooden furniture was an important medium for decorative designs, mostly carved right into the timber.
        In ancient China people sat, squatted, and slept on the floor or on a <span class='bookTitle'>kang</span> <a href='/word_detail.php?id=4749' onmouseover="showToolTip(this, 'kàng', 'a heatable brick bed')" 
        onmouseout="hideToolTip()">炕</a> (a heatable brick bed) in Northern China.
        In fact, most Chinese feel comfortable squatting on flat feet today while Europeans cannot squat on flat feet at all.
        Ancient furniture was designed from this perspective.
        Furniture with high legs, such as the chairs and tables that we are familiar with today, was introduced during the Tang.
        Modern furniture with exquisite art work became widespread in the Ming with the appearance of fine timber from Southeast
        Asia import via ocean going ships and the development of modern carpentry tools to make the furniture in efficiently. 
      </p>
      <p>
        Architecture was also an important use of wood and often had decorations carved into the wood.
        This includes items such as doors, window frames, columns, timber roofs, and wall panels.
        Two great places to see traditional Chinese furniture and timber architecture are the UNESCO World Heritage Sites the
        <span class='bookTitle'>Humble Administrator's Garden</span> <a href='/word_detail.php?id=25651' onmouseover="showToolTip(this, '拙政園 Zhuōzhèng Yuán', 'Humble Administrator&apos;s Garden')" 
        onmouseout='hideToolTip()'>拙政园</a> and the <span class='bookTitle'>Lion Forest Garden</span> <a href='/word_detail.php?id=25630' onmouseover="showToolTip(this, '獅子林 Shīzi Lín', 'Lion Forest Garden / Lion Grove Garden')" 
        onmouseout='hideToolTip()'>狮子林</a>, both in Suzhou.
      </p>
      <p>
        Nails and metal fittings were not used during the Ming in furniture or in buildings.  
        All elements were connected with mortise and tenon joints.
      </p>
      <p>
        Bamboo furniture and carving is an art that is very distinctly Chinese.
        However, bamboo rots quickly.
        <span class='bookTitle'>Carved bamboo with light color relief</span> <a href='/word_detail.php?id=26000' onmouseover="showToolTip(this, 'liú qīng zhú kè', 'carved bamboo with light color relief')" 
        onmouseout='hideToolTip()'>留青竹刻</a> is a style of relief carving on bamboo where the raised portion is shown in 
        the lighter color of the remaining bamboo skin.
        The picture below shows a landscape scene on a carved bamboo wrist rest.
      </p>
