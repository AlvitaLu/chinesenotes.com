        Song Dynasty
        <a href='/word_detail.php?id=8739' onmouseover="showToolTip(this, 'Sòng', 'Song Dynasty')" onmouseout='hideToolTip()'>宋</a>
      </h3>
      <p>
        The Song Dynasty (960—1279) was a prosperous period in China during which cities flourished, many inventions where made,
        such as gunpowder and movable type printing, and porcelain became more widespread.
        There were many developments in form, glaze techniques, and artistic content.
        Of the many porcelain factories in China at this time, and the best know were the 
        <span class='bookTitle'>Five Famous Kilns of the Song Dynasty</span> <a href='/word_detail.php?id=25645' onmouseover="showToolTip(this, '宋代五大名窯 Sòng Dài Wŭ Dà Míng Yáo', 'Five Famous Kilns of the Song Dynasty')" 
        onmouseout="hideToolTip()">宋代五大名窑</a>,
        which were: <span class='bookTitle'>Ru Kiln</span> <a href='/word_detail.php?id=25644' onmouseover="showToolTip(this, '汝窯 Rǔ Yáo', 'Ru Kiln')" 
        onmouseout="hideToolTip()">汝窑</a> in present day Linru, Henan; <span class='bookTitle'>Guan Kiln</span> <a href='/word_detail.php?id=25646' onmouseover="showToolTip(this, '官窯 Guān Yáo', 'Guan Kiln')" 
        onmouseout='hideToolTip()'>官窑</a>; <span class='bookTitle'>Ge Kiln</span> <a href='/word_detail.php?id=25647' onmouseover="showToolTip(this, '哥窯 Gē Yáo', 'Ge Kiln')" 
        onmouseout='hideToolTip()'>哥窑</a>; <span class='bookTitle'>Jun Kiln</span> <a href='/word_detail.php?id=25648' onmouseover="showToolTip(this, '鈞窯 Jūn Yáo', 'Jun Kiln')" 
        onmouseout='hideToolTip()'>钧窑</a> in present day Yuxian County, Henan; and <span class='bookTitle'>Ding Kiln</span> <a href='/word_detail.php?id=25649' onmouseover="showToolTip(this, '定窯 Dìng Yáo', 'Ding Kiln')" 
        onmouseout='hideToolTip()'>定窑</a> in present day Quyuan County, Hebei.
      </p>
