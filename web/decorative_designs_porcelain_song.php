      <p>
        <span class='bookTitle'>Longquan Kiln</span> <a href='/word_detail.php?id=25722' onmouseover="showToolTip(this, '龍泉窯 Lóngquán Yáo', 'Longquan Kiln')" 
        onmouseout="hideToolTip()">龙泉窑</a> located in present day Longquan, Zhejiang and founded in the Five Dynasties period, 
        is one of the most famous porcelain kilns in China, 
        The kiln is known for the powder-green and plum-green colors obtained by applying multiple layers of glaze.
        The jar below was produced at <span class='bookTitle'>Longquan Kiln</span>.
        Longquan kiln was well known for producing soft colors and forms in a subtle variety of greens and blues.
        A special alkaline-calcarous glaze was used rather than the standard lime glaze to allow thicker and more even application.
      </p>
