      <h3 class='article'>
        <a name="embroidery"></a>
        Embroidery
        <a href='/word_detail.php?id=13529' onmouseover="showToolTip(this, '繡 xiù​', 'to embroider')" onmouseout='hideToolTip()'>绣</a>
      </h3>
      <p>
        Embroidery is a method using thread to sew a decorative design on silk or cotton cloth.
        Because of the history of silk in China, Chinese embroidery is done on silk
        It dates back to the Neolithic, when silk was developed.
        There are <span class='bookTitle'>four main regional styles of Chinese embroidery</span> <a href='/word_detail.php?id=25902' onmouseover="showToolTip(this, '四大名繡 sì​dà míng xiù', 'four regional styles of embroidery')" 
        onmouseout="hideToolTip()">四大名绣</a>, which incude <span class='bookTitle'>Suzhou Emrboidery</span> <a href='/word_detail.php?id=25900' onmouseover="showToolTip(this, '蘇繡 Sū xiù​', 'Suzhou Embroidery')" 
        onmouseout="hideToolTip()">苏绣</a>, <span class='bookTitle'>Hunan Embroidery</span> <a href='/word_detail.php?id=25908' onmouseover="showToolTip(this, '湘繡 Xiāng Xiù', 'Hunan Embroidery')" 
        onmouseout="hideToolTip()">湘绣</a>, <span class='bookTitle'>Guangdong Embroidery</span> <a href='/word_detail.php?id=25909' onmouseover="showToolTip(this, '粵繡 Yuè xiù', 'Guangdong Embroidery')" 
        onmouseout="hideToolTip()">粤绣</a>, and <span class='bookTitle'>Sichuan Embroidery</span> <a href='/word_detail.php?id=25910' onmouseover="showToolTip(this, '蜀繡 Shǔ Xiù', 'Sichuan Embroidery')" 
        onmouseout="hideToolTip()">蜀绣</a>.
        Apart from these, there are also other regional styles.
        <span class='bookTitle'>Sichuan Embroidery</span> has the longest history.
        A piece of silk embroidery dating circa 1,000 BCE was found in a tomb in Egypt and has been confirmed as sewn in 
        a style from Sichuan.
        Artifacts found in the <span class='bookTitle'>Three Stars Mound</span> <a href='/word_detail.php?id=23005' onmouseover="showToolTip(this, 'Sānxīng Duī', 'Three Stars Mound')" 
        onmouseout="hideToolTip()">三星堆</a> in Sichuan and also other sites in China confirm silk production in the Neolithic.
        Other ethnic groups, including the Bai, Miao, Zhuang and Tibetan people have their own styles of embroidery.
        The <span class='bookTitle'>four main regional styles of Chinese embroidery</span> emerged in the 19th century.
      </p>
      <p>
        The dragon below was embroidered with <span class='bookTitle'>flat gold</span> <a href='/word_detail.php?id=25888' onmouseover="showToolTip(this, 'píngjīn', 'flat gold embroidery')" 
        onmouseout='hideToolTip()'>平金</a>, a technique developed in Suzhou, that uses gold thread to create a 
        slightly protruding flat design.
        The needle work method used is <span class='bookTitle'>petit-point</span> <a href='/word_detail.php?id=25898' onmouseover="showToolTip(this, '納紗 nà shā', 'petit-point')" 
        onmouseout='hideToolTip()'>纳纱</a>.
      </p>
