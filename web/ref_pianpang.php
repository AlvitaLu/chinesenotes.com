      <p>
        Pianpang are components of characters that are made up of more than one part.
        Originally, the left part of composite characters was referred to as “pian” and the right side referred to as “pang.”
        Now, all parts of compound characters are generally referred to as “pianpang.”
        For example, the left part of a compound character is referred to as “zuo pianpang” and the right side the “you pianpang.”
      </p>
