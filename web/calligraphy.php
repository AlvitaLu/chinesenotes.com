      <h2 class='article'>
        <a name="introduction"></a><a href='/word_detail.php?id=9236' onmouseover="showToolTip(this, 'dǎoyán', 'introduction / preamble')" onmouseout="hideToolTip()">导言</a>
        Introduction
      </h2>
      <p>
        I started taking notes on calligraphy (<a href='/word_detail.php?id=3433' onmouseover="showToolTip(this, 'shūfǎ', 'calligraphy')" onmouseout="hideToolTip()">书法</a> or <a href='/word_detail.php?id=17233' onmouseover="showToolTip(this, 'shūhuà', 'calligraphy')" onmouseout="hideToolTip()">书画</a>)
        for three reasons.  Firstly, I enrolled in a calligraphy
        class at the South Coast Chinese Cultural Association 
        (<a href='/word_detail.php?id=1805' onmouseover="showToolTip(this, 'nán', 'south')" onmouseout="hideToolTip()">南</a><a href='/word_detail.php?id=14893' onmouseover="showToolTip(this, 'hǎi àn', 'coast')" onmouseout="hideToolTip()">海岸</a><a href='/word_detail.php?id=2550' onmouseover="showToolTip(this, 'Zhōnghuá', 'China')" onmouseout="hideToolTip()">中华</a><a href='/word_detail.php?id=1412' onmouseover="showToolTip(this, 'wénhuà', 'culture')" onmouseout="hideToolTip()">文化</a><a href='/word_detail.php?id=5952' onmouseover="showToolTip(this, 'xiéhuì', 'an association / a society')" onmouseout="hideToolTip()">协会</a>,
        <a href='http://www.sccca.us/'>www.sccca.us</a>) in California.  
        The class was entirely in Chinese and I
        needed to take notes on some of the special vocabulary relating to calligraphy use in the class.  
        Thanks to my teacher Chen Rong.
        Secondly, I now have only very seldom occasion to write characters out by hand.  I use a 
        computer keyboard almost exclusively for writing so that I am not remembering how 
        the strokes go together to form characters.  I hope that calligraphy will help me to remember
        better. 
        Thirdly, I wanted to have a deeper understanding of Chinese writing for use of fonts
        and Calligraphy on my own web site.
        I hope that these notes will be able to help other people in similar aims and circumstances.
      </p>
      <p>
        When you start learning calligraphy you will most likely start with <a href='/word_detail.php?id=2599' onmouseover="showToolTip(this, 'kǎishū', 'regular script')" onmouseout="hideToolTip()">楷书</a>
        regular script.  Other styles to learn with further study are
        <a href='/word_detail.php?id=2598' onmouseover="showToolTip(this, 'cǎoshū', 'cursive script')" onmouseout="hideToolTip()">草书</a>
        cursive script，<a href='/word_detail.php?id=2601' onmouseover="showToolTip(this, 'dàzhuàn', 'great seal script')" onmouseout="hideToolTip()">大篆</a>
        great seal script，<a href='/word_detail.php?id=2600' onmouseover="showToolTip(this, 'xiǎozhuàn', 'small seal script')" onmouseout="hideToolTip()">小篆</a>
        small seal script，<a href='/word_detail.php?id=2593' onmouseover="showToolTip(this, 'xíngshū', 'semi-cursive script')" onmouseout="hideToolTip()">行书</a>
        semi-cursive script，<a href='/word_detail.php?id=2597' onmouseover="showToolTip(this, 'lìshū', 'clerical script')" onmouseout="hideToolTip()">隶书</a>
        clerical script，and <a href='/word_detail.php?id=2595' onmouseover="showToolTip(this, 'jiǎgǔwén', 'oracle bone script')" onmouseout="hideToolTip()">甲骨文</a>
        oracle bone script.
      </p>
      <p>
        The most common style of regular script for beginners to learn is 
        <a href='/word_detail.php?id=17226' onmouseover="showToolTip(this, 'yántǐ', 'Yan Style')" onmouseout="hideToolTip()">颜体</a>
        Yan style.  This is a style after the well known Tang calligrapher 
        <a href='/word_detail.php?id=7809' onmouseover="showToolTip(this, 'Yán Zhēnqīng', 'Yan Zhenqing')" onmouseout='hideToolTip()'>颜真卿</a>
        Yan Zhenqing (709–785) that is often followed in many introductory calligraphy classes and 
        texts.  For more on the life and works of Yan Zhenqing see the book
        Yan Qin Ceremony Inscription
        <a href='calligraphy_references.php'>[ZHAN]</a>.
        The other three calligraphics styles (<a href='/word_detail.php?id=2552' onmouseover="showToolTip(this, 'shūtǐ', 'calligraphic style')" onmouseout="hideToolTip()">书体</a>)from the <a href='/word_detail.php?id=17247' onmouseover="showToolTip(this, 'kǎishū sì dà jiā', 'The four great masters of regular script')" onmouseout="hideToolTip()">楷书四大家</a>
        four great masters of regular script are
        <a href='/word_detail.php?id=17241' onmouseover="showToolTip(this, 'Ōu tǐ', 'calligraphic style of Ouyang Xun')" onmouseout="hideToolTip()">欧体</a>
        Ou style after <a href='/word_detail.php?id=17222' onmouseover="showToolTip(this, 'Ōuyáng Xún', 'Ouyang Xun')" onmouseout="hideToolTip()">欧阳询</a>
        Ouyang Xun, <a href='/word_detail.php?id=17242' onmouseover="showToolTip(this, 'Liǔ tǐ', 'calligraphic style of Liu Gongquan')" onmouseout="hideToolTip()">柳体</a>
        Liu style after <a href='/word_detail.php?id=17243' onmouseover="showToolTip(this, 'Liǔ Gōngquán', 'Liu Gongquan')" onmouseout="hideToolTip()">柳公权</a>
        Liu Gongquan, and <a href='/word_detail.php?id=17244' onmouseover="showToolTip(this, 'Zhào tǐ', 'calligraphic style of Liu Gongquan')" onmouseout="hideToolTip()">赵体</a>
        Zhao style after <a href='/word_detail.php?id=17245' onmouseover="showToolTip(this, 'Zhào Mèngfǔ', 'Zhao Mengfu')" onmouseout="hideToolTip()">赵孟頫</a>
        Zhao Mengfu.  
        For more on Ouyang Xun see the book 
        Ouyang Xun, Introduction to Regular Script
        <a href='calligraphy_references.php'>[SHI1]</a>.
        When starting calligraphy is best to stick with one style until you are at least
        familiar with the fundamental strokes for the style until you try learning other styles.
      </p>
