      <h2 class='article'>
        <a name="yanzhenqing"></a><a href='/word_detail.php?id=7809' onmouseover="showToolTip(this, 'Yán Zhēnqīng', 'Yan Zhenqing')" onmouseout="hideToolTip()">颜真卿</a>
        Yan Zhenqing
      </h2>
      <p>
        Yan Zhenqing was a senior court official who passed the imperial examinations at the time of
        Emperor Xuanzong. Because of a scandal in the government he was demoted to the governor of 
        an ordinary province.
        In the An Lushan rebellion he joined with his cousin to Yan Gaoqing, raising an army to quell a 
        disturbance. Seventeen neighboring districts responded to his rally. However, in the process he 
        allied himself with them.  
        When he returned to the capital afterwards all the high officials and royals, 
        including Yan Zhenqing, were sent out to unimportant, outlying districts.
        Afterwards, he was commonly called <a href='/word_detail.php?id=18250' onmouseover="showToolTip(this, 'Yán Lǔgōng', 'Yan Lugong / Yan Zhenqing')" 
        onmouseout="hideToolTip()">颜鲁公</a> Yan Lugong or <a href='/word_detail.php?id=18251' onmouseover="showToolTip(this, 'Yán Píngyuán', 'Yan Pingyuan / Yan Zhenqing')" 
        onmouseout='hideToolTip()'>颜平原</a> Yan Pingyuan.
        Because he had conflicts, while acting in his official position, with powerful officials on a number of occasions he suffered harm.
      </p>
      <p>
        At the time of <a href='/word_detail.php?id=18254' onmouseover="showToolTip(this, 'Dé Zōng', 'Emperor De Zong')" 
        onmouseout='hideToolTip()'>德宗</a> Emperor De Zong, during the <a href='/word_detail.php?id=18256' onmouseover="showToolTip(this, 'Lǐ Xīliè', 'Li Xilie')" 
        onmouseout='hideToolTip()'>李希烈</a> Li Xilie armed rebellion, prime minister <a href='/word_detail.php?id=18259' onmouseover="showToolTip(this, 'Lú Qǐ', 'Lu Qi')" 
        onmouseout='hideToolTip()'>卢杞</a> Lu Qi remembered his grudge against Yan Zhenqing and dispatched him
        to a rebel area.  Yan Zhenqing loyally obeyed the order but in the end he was murdered there.
      </p>
