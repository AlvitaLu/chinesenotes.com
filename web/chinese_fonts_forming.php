      <h3 class='article'>
        <a name="formation"></a>
        <a href='/word_detail.php?id=25253' onmouseover="showToolTip(this, 'zào zì', 'to form characters')" onmouseout="hideToolTip()">造字</a><a href='/word_detail.php?id=5616' onmouseover="showToolTip(this, 'yuánlǐ', 'principle / theory')" 
        onmouseout="hideToolTip()">原理</a>
      	Formation of Characters
      </h3>
      <p>
        The Six Methods of forming Chinese characters <a href='/word_detail.php?id=17869' onmouseover="showToolTip(this, '六書 liù​ shū​', 'Six Methods of forming Chinese characters')" 
        onmouseout='hideToolTip()'>六书</a> described in the historic classic <span class='bookTitle'>Shuo Wen Jie Zi</span> <a href='/word_detail.php?id=17080' onmouseover="showToolTip(this, '說文解字 Shuō Wén Jiě Zì​', 'Shuo Wen Jie Zi')" 
        onmouseout="hideToolTip()">说文解字</a> compiled in around 100 CE
        are still considered fundamental to the principles of forming Chinese characters.
        The six types are: Pictogram <a href='/word_detail.php?id=17870' onmouseover="showToolTip(this, 'xiàngxíng', 'pictogram')" 
        onmouseout='hideToolTip()'>象形</a>, Ideogram <a href='/word_detail.php?id=17868' onmouseover="showToolTip(this, 'zhǐshì​', 'ideogram')" 
        onmouseout='hideToolTip()'>指事</a>, Combined Ideogram <a href='/word_detail.php?id=17872' onmouseover="showToolTip(this, '會意 huì​ yì​', 'combined ideogram')" 
        onmouseout='hideToolTip()'>会意</a>, Phonogram <a href='/word_detail.php?id=17871' onmouseover="showToolTip(this, '形聲 xíng shēng', 'phonogram')" 
        onmouseout='hideToolTip()'>形声</a>, Transfer character <a href='/word_detail.php?id=17873' onmouseover="showToolTip(this, '轉註 zhuǎn zhù​', 'transfer character')" 
        onmouseout='hideToolTip()'>转注</a>, and Loan character <a href='/word_detail.php?id=21621' onmouseover="showToolTip(this, 'jiǎ jiè', 'a loan character')" 
        onmouseout='hideToolTip()'>假借</a>.
      </p>
      <h4 class='article'>
        <a name="pictogram"></a>
        Pictogram <a href='/word_detail.php?id=17870' onmouseover="showToolTip(this, 'xiàngxíng', 'pictogram')" 
          onmouseout='hideToolTip()'>象形</a>
      </h4>
      <p>
        A pictogram is a character derived from a picture.
        These characters arose from Oracle bone script.
        There are only about 300 pictograms in Chinese but they are very important in origins of the language.
        Examples are <a href='/word_detail.php?id=8319' onmouseover="showToolTip(this, 'rì​', 'day / sun')" 
        onmouseout='hideToolTip()'>日</a> sun, <a href='/word_detail.php?id=3185' onmouseover="showToolTip(this, 'yuè', 'moon')" 
        onmouseout='hideToolTip()'>月</a> moon, and <a href='/word_detail.php?id=3122' onmouseover="showToolTip(this, 'shān', 'mountain / hill')" 
        onmouseout='hideToolTip()'>山</a> mountain.
      </p>
      <h4 class='article'>
        Ideogram <a href='/word_detail.php?id=17868' onmouseover="showToolTip(this, 'zhǐshì​', 'ideogram')" 
        onmouseout='hideToolTip()'>指事</a>
      </h4>
      <p>
        An ideogram is a character indicating an idea
      </p>
      <h4 class='article'>
        Combined Ideogram <a href='/word_detail.php?id=17872' onmouseover="showToolTip(this, '會意 huì​ yì​', 'combined ideogram')" 
        onmouseout='hideToolTip()'>会意</a>
      </h4>
      <p>
        A combined ideogram is a character that combines the meanings of existing characters as elements.
      </p>
      <h4 class='article'>
        Phonogram <a href='/word_detail.php?id=17871' onmouseover="showToolTip(this, '形聲 xíng shēng', 'phonogram')" 
        onmouseout='hideToolTip()'>形声</a>
      </h4>
      <p>
        A phonogram is a character derived from a radical indicating meaning plus phonetic part.
      </p>
      <h4 class='article'>
        Transfer character <a href='/word_detail.php?id=17873' onmouseover="showToolTip(this, '轉註 zhuǎn zhù​', 'transfer character')" 
        onmouseout='hideToolTip()'>转注</a>
      </h4>
      <p>
        A transfer character is a character with meanings influenced by other characters.
      </p>
      <h4 class='article'>
        Loan character <a href='/word_detail.php?id=21621' onmouseover="showToolTip(this, 'jiǎ jiè', 'a loan character')" 
        onmouseout='hideToolTip()'>假借</a>
      </h4>
      <p>
        A loan character is a character acquiring meanings by phonetic association.
      </p>
      <p>
        For more on the formation of Chinese characters see the book <span class='bookTitle'>Chinese Characters</span>
        [<a href='chinese_fonts_ref.php'>Han Jianting 2008</a>].
      </p>
