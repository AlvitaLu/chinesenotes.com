        <a name="qing"></a>
        Qing Dynasty
        <a href='/word_detail.php?id=3356' onmouseover="showToolTip(this, 'Qīng', 'Qing Dynasty')" onmouseout='hideToolTip()'>清</a>
      </h3>
      <p>
        Yellow porcelain roof tiles were used to cover structures associated with the emperor.
        Palace roofs use special glazed roof tiles called <span class='bookTitle'>liuli way</span> <a href='/word_detail.php?id=19714' onmouseover="showToolTip(this, 'liú​lí ​wǎ', 'a glazed roof tile')" 
        onmouseout="hideToolTip()">琉璃瓦</a> that were for the exclusive use of the emperor.
        They took 180 days to make with a special purification process and two firings.
        The tile ends in the Forbidden City feature stamps with a dragon design.
        Over a hundred million tiles were used in building the Forbidden City in Beijing.
        The picture below shows a few of them.
      </p>
