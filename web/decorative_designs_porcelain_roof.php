      <p>
        <span class='bookTitle'>Celestial beings and roof beasts</span> are used to decorate roofs in palaces and 
        temples.
        The use of nine of these in a row is reserved for the emperor.
        However, the Taihe Hall is unique in having 10 animals, making a total of 13 with the immortal gardian
        at the end.
        The celestial beings and roof beasts on the Taihe Hall are shown below.
