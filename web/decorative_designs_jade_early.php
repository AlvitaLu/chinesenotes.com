      <h3 class='article'>
        <a name="early"></a>
        Early Period
        <a href='/word_detail.php?id=13726' onmouseover="showToolTip(this, 'zǎoqī', 'early period / early phase / early stage')" 
        onmouseout="hideToolTip()">早期</a>
      </h3>
      <p>
        Many early jade artifacts were discovered from the <span class='bookTitle'>Hongshan Culture</span> <a href='/word_detail.php?id=25527' onmouseover="showToolTip(this, '紅山文化 Hóngshān Wénhuà', 'Hongshan Culture')" 
        onmouseout='hideToolTip()'>红山文化</a> that existed in Northeast China from about 4,700 to 2900 BCE.
        Later on the <span class='bookTitle'>Liangzhu Culture</span> (c. 3400－2250 BCE) in the Yantze Delta also produced many
        jade artifacts.
        The types of artifacts were mostly small and decorative, such as pendants, bracelets, and hair pins, 
        as appropriate to the use of jade as a medium for art.
      </p>
