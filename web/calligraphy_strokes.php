      <h2 class='article'>
        <a name="writing"></a>
        <a href='/word_detail.php?id=17263' onmouseover="showToolTip(this, 'bǐfǎ', 'calligraphic technique / drawing strokes')" onmouseout="hideToolTip()">笔法</a>
        Drawing Strokes
      </h2>
      <p>
        Drawing a stroke (<a href='/word_detail.php?id=17263' onmouseover="showToolTip(this, 'bǐfǎ', 'calligraphic technique / drawing strokes')" onmouseout="hideToolTip()">笔法</a>)
        involves the start of the stroke (<a href='/word_detail.php?id=17290' onmouseover="showToolTip(this, 'qǐbǐ', 'the start of a stroke')" onmouseout="hideToolTip()">起笔</a>),
        moving the brush (<a href='/word_detail.php?id=17291' onmouseover="showToolTip(this, 'xíngbǐ', 'moving the brush')" onmouseout="hideToolTip()">行笔</a>),
        to form the main part of the stroke, and finishing the stroke (<a href='/word_detail.php?id=17292' onmouseover="showToolTip(this, 'shōubǐ', 'the end of a stroke')" onmouseout="hideToolTip()">收笔</a>).
        Two basic skills are moving the tip of the brush (<a href='/word_detail.php?id=2070' onmouseover="showToolTip(this, 'bǐ', 'pen / pencil / writing brush / the strokes of Chinese characters')" onmouseout="hideToolTip()">笔</a><a href='/word_detail.php?id=13111' onmouseover="showToolTip(this, 'fēng', 'the point of a spear / the edge of a tool / the vanguard')" onmouseout="hideToolTip()">锋</a>) 
        up in a kind of slow flicking motion (<a href='/word_detail.php?id=2581' onmouseover="showToolTip(this, 'tí', 'a flick up and rightwards in a character')" onmouseout='hideToolTip()'>提</a><a href='/word_detail.php?id=2070' onmouseover="showToolTip(this, 'bǐ', 'pen / pencil / writing brush / the strokes of Chinese characters')" onmouseout='hideToolTip()'>笔</a>)
        and to press the tip of the brush down against the paper (<a href='/word_detail.php?id=1985' onmouseover="showToolTip(this, 'àn', 'to fulfill / to press / to push / to control')" onmouseout="hideToolTip()">按</a><a href='/word_detail.php?id=2070' onmouseover="showToolTip(this, 'bǐ', 'pen / pencil / writing brush / the strokes of Chinese characters')" onmouseout="hideToolTip()">笔</a>). 
        These two motions control the thickness of strokes.
      </p>
      <p>
        Some strokes, such as pie (downwards-left curved stroke <a href='/word_detail.php?id=2584' onmouseover="showToolTip(this, 'piě', 'downwards-left curved character stroke')" onmouseout="hideToolTip()">撇</a>),
        na (downwards-right concave character stroke <a href='/word_detail.php?id=2585' onmouseover="showToolTip(this, 'nà', 'downwards-right concave character stroke')" onmouseout="hideToolTip()">捺</a>),
        and gou (a hook shaped stroke <a href='/word_detail.php?id=12172' onmouseover="showToolTip(this, 'gōu', 'a hook shaped stroke')" onmouseout='hideToolTip()'>钩</a>)
        finish with a hook.
      </p>
