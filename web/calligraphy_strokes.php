      <p>
        Some characters require a hook-like motion to start a stroke (<a href='/word_detail.php?id=17297' onmouseover="showToolTip(this, 'nì fēng qǐbǐ', 'a hooked shaped movement of the brush to start a stroke')" onmouseout="hideToolTip()">逆锋起笔</a>).
        Some characters require a hook-like movement at the end of the stroke (<a href='/word_detail.php?id=17299' onmouseover="showToolTip(this, 'huí fēng shōubǐ', 'a hooked-like movement of the brush at the end of a stroke')" onmouseout="hideToolTip()">回锋收笔</a>).
      </p>
      <p>
        Starting a stroke in a very abrupt way with a straight end leads to a square style (<a href='/word_detail.php?id=3382' onmouseover="showToolTip(this, 'fāng', 'square / quadrilateral / direction / one side')" onmouseout="hideToolTip()">方</a><a href='/word_detail.php?id=2070' onmouseover="showToolTip(this, 'bǐ', 'pen / pencil / writing brush / the strokes of Chinese characters')" onmouseout="hideToolTip()">笔</a>).
        Ouyang Xun (<a href='/word_detail.php?id=17222' onmouseover="showToolTip(this, 'Ōuyáng Xún', 'Ouyang Xun')" onmouseout='hideToolTip()'>欧阳询</a>)
        was known for this style.
      </p>
