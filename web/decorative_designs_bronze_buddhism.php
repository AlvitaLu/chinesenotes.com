        Usually, if one simply writes <span class='bookTitle'>the Buddha</span>, then 
        <span class='bookTitle'>Sakyamuni Buddha</span> is intended.
        Bronze has a history of over 1500 years for Buddhist artifacts, mostly for statues, incense burners, and bells.
        The statue below is an early one from the Northern Wei.
        The striated pattern on the <span class='bookTitle'>nimbus</span> or halo <a href='/word_detail.php?id=25824' onmouseover="showToolTip(this, 'bèiguāng', 'a nimbus / a halo')" 
