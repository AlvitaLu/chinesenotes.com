      <p>
        Incense symbolizes purification.
        The incense burner below is typical for a large temple.
        incense burners usually feature intricate artwork and, in some cases, can be about two stories high, reflecting the
        importance of burning incense in Chinese Buddhism.
        The incense burner below is two meters tall, cast copper and is located in front of the Great Hall of the Heros at 
        Lingyin Temple.
        It was donated by Ming Palace Eunuch Sun Long <a href='/word_detail.php?id=12109' onmouseover="showToolTip(this, '太監 tàijiàn', 'palace eunuch')" onmouseout="hideToolTip()">太监</a><a href='/word_detail.php?id=2786' onmouseover="showToolTip(this, '孫 Sūn', 'Sun')" onmouseout="hideToolTip()">孙</a><a href='/word_detail.php?id=8471' onmouseover="showToolTip(this, 'lóng', 'grand / intense / prosperous')" 
