      <p>
        Besides <span class='bookTitle'>Sakyamuni Buddha</span>, other buddhas are mentioned in Buddhist sutras.
        According to the <span class='bookTitle'>Amitabha Sutra</span> <a href='/word_detail.php?id=18668' onmouseover="showToolTip(this, '阿彌陀經 Āmítuó Jīng', 'The Amitabha Sutra')" 
        onmouseout="hideToolTip()">阿弥陀经</a>,
        <span class='bookTitle'>Amitabha Buddha</span> <a href='/word_detail.php?id=20320' onmouseover="showToolTip(this, '阿彌陀 Āmí​tuó', 'Amitabha / Amithaba')" 
        onmouseout='hideToolTip()'>阿弥陀</a> is the founder of the <span class='bookTitle'>Western Pure Land</span> <a href='/word_detail.php?id=25832' onmouseover="showToolTip(this, '西方極樂世界 Xīfāng Jílè Shìjiè', 'Western Pure Land')" 
        onmouseout='hideToolTip()'>西方极乐世界</a>.
        Chanting <span class='bookTitle'>Amitabha</span> Buddha's name is a common practice for the 
        <span class='bookTitle'>Pure Land School</span> <a href='/word_detail.php?id=18059' onmouseover="showToolTip(this, '凈土宗 Jì​ng Tŭ Zōng', 'Pure Land School')" 
        onmouseout="hideToolTip()">净土宗</a> of Buddhism, which is one of the most common schools of Chinese Buddhism.
        In the bronze artifact below shows three Amitabha Buddha figures with hands showing
        <span class='bookTitle'>speaking the Dharma mudra</span> <a href='/word_detail.php?id=25819' onmouseover="showToolTip(this, '說法印 shuōfa yìn', 'speaking the Dharma mudra')" 
        onmouseout="hideToolTip()">说法印</a>, indicating that Amitabha is expounding the teachings of the Buddha.
      </p>
