        <a name="architecture"></a>
        Architecture
        <a href='/word_detail.php?id=919' onmouseover="showToolTip(this, '建築 jiànzhù', 'building / construction / architecture')" onmouseout='hideToolTip()'>建筑</a>
      </h3>
      <p>
        <span class='bookTitle'>Garden architecture</span> <a href='/word_detail.php?id=25913' onmouseover="showToolTip(this, '園林建築 yuánlín jiànzhù', 'garden architecture')" 
        onmouseout='hideToolTip()'>园林建筑</a> refers to the various architectural structures used in parks and gardens, such
        as <span class='bookTitle'>pavilions</span> <a href='/word_detail.php?id=9064' onmouseover="showToolTip(this, 'tíng', 'pavilion')" 
        onmouseout='hideToolTip()'>亭</a> (covered structures without walls)，
         <span class='bookTitle'>xie</span> (pavilions on terraces) <a href='/word_detail.php?id=25914' onmouseover="showToolTip(this, 'xiè', 'a pavilion on terrace')" 
        onmouseout='hideToolTip()'>榭</a>, corridors <a href='/word_detail.php?id=7779' onmouseover="showToolTip(this, 'láng', 'corridor / veranda / porch')" 
        onmouseout='hideToolTip()'>廊</a>, <span class='bookTitle'>ge</span> (two story pavilions) <a href='/word_detail.php?id=25915' onmouseover="showToolTip(this, '閣 gé', 'two story pavilion')" 
        onmouseout='hideToolTip()'>阁</a>, <span class='bookTitle'>xuan</span> (pavilions with a view) <a href='/word_detail.php?id=25906' onmouseover="showToolTip(this, '軒 xuān', 'a pavilion with a view')" 
        onmouseout='hideToolTip()'>轩</a>, storied buildings <a href='/word_detail.php?id=3803' onmouseover="showToolTip(this, '樓 lóu', 'storied building')" 
        onmouseout='hideToolTip()'>楼</a>, terraces <a href='/word_detail.php?id=8302' onmouseover="showToolTip(this, '檯 tái', 'desk / platform / terrace')" 
        onmouseout='hideToolTip()'>台</a>, and <span class='bookTitle'>fang</span> (buildings on water) <a href='/word_detail.php?id=25917' onmouseover="showToolTip(this, 'fǎng', 'a building built on water')" 
        onmouseout='hideToolTip()'>舫</a>.
        Chinese garden architecture was highly developed in classical times and, as you can see from this list, 
        there are no good English terms to translate to for the many different kinds of pavilions.  
      </p>
