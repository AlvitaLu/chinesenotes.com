      <p>
        The <span class='bookTitle'>Humble Administrator's Gardens</span> <a href='/word_detail.php?id=25651' onmouseover="showToolTip(this, '拙政園 Zhuōzhèng Yuán', 'Humble Administrator&apos;s Garden')" 
        onmouseout='hideToolTip()'>拙政园</a> are one of the finest gardens in China and one of the best places to see
        traditional Chinese architecture, especially wooden structures.
        In 1997, along with other classical Chinese gardens in Suzhou, <span class='bookTitle'>Humble Administrator's Garden</span>,
        was listed as a UNESCO World Heritage Site.
        The <span class='bookTitle'>Humble Administrator's Gardens</span> are the largest of the classical gardens in Suzhou.
        The site of the gardens was originally the residence of Tang scholar Lu Guimeng.
        In 1513 Ming Imperial Envoy and poet Wang Xiancheng appropriated the property and began work on the gardens.
        Cao Xueqin <a href='/word_detail.php?id=20638' onmouseover="showToolTip(this, 'Cáo​ Xuě​qín​', 'Cao Xueqin')" 
        onmouseout="hideToolTip()">曹雪芹</a>, author of <span class='bookTitle'>A Dream of Red Mansions</span> <a href='/word_detail.php?id=20637' onmouseover="showToolTip(this, '紅樓夢 Hóng ​Lóu​ Mèng', 'A Dream of Red Mansions')" 
        onmouseout="hideToolTip()">红楼梦</a> (c. 1791), lived at the gardens in his teen years and is believed to be an influence
        on the novel.
        The most enchanting thing about the gardens is the aesthetic ideal of harmony between nature and people.
      </p>
