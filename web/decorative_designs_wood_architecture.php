      <p>
        Relief carvings on wood panels like the one below are common on door and wall panels in historic Chinese buildings.
        In fact, the name for <span class='bookTitle'>door panel</span> <a href='/word_detail.php?id=26319' onmouseover="showToolTip(this, 'huābǎn', 'door panel')" 
        onmouseout='hideToolTip()'>花板</a> literally means decorated panel, reflecting the fact that they are nearly always 
        decorated.
        In homes of important officials no two door or wall panels would be identical.
      </p>
