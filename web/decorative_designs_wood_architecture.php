      <p>
        The gardens today mostly resemble their appearance in the late Qing.
        The numerous pavilions and halls are most interesting for their wooden architecture.
        There are many decorative elements in the architectural structure and they are fantastic amongst the background of the 
        gardens.
        I particularly like the windows, which look out into the brilliant sunlight in the gardens. 
        Traditional Chinese architecture often uses an open screen window <a href='/word_detail.php?id=25926' onmouseover="showToolTip(this, 'lòuchuāng', 'an open screen window')" 
        onmouseout='hideToolTip()'>漏窗</a> with the screen made using a decorative design.
        Open screen windows are a common feature in classical Chinese garden architecture.
        They separate distinct areas with making them feel inaccessible.
        An example of this from the <span class='bookTitle'>Humble Administrator's Garden</span> is shown below.
      </p>
