      <p>
        The different designs of the windows of the screens themselves add a lot of interest to the gardens and the building
        structures.
        There are many different kinds of designs for the open screen window, the most popular being geometric patterns,
        flower and plant designs, landscape pictures, and animals.
        One of the most popular designs, especially in the Suzhou area, is the 
        <span class='bookTitle'>cracked ice pattern</span> <a href='/word_detail.php?id=25932' onmouseover="showToolTip(this, 'bīngliè', 'cracked ice pattern')" 
        onmouseout='hideToolTip()'>冰裂</a> shown below.
      </p>
