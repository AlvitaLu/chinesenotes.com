      <p>
        One of the characteristics of the Humble Administrator's Garden is the large amount of water area,
        whichs gives the garden a natural and peaceful quality.
        The pavilions and other structures are arranged around these ponds and streams.
        The open windows, corridors, flower beds, stone gardens, old trees, green bamboo create tranquility and seclusion,
        while still being connected and interesting.
        This arrangement is characteristic of Ming garden style.
      </p>
