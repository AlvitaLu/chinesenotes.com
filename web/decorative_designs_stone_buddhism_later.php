        <span class='bookTitle'>Great Freedom Dharani</span> <a href='/word_detail.php?id=26044' onmouseover="showToolTip(this, '大自在陀羅尼神咒經 Dà Zì​zai Tuóluóní Shén Zhòu Jīng', 'Great Freedom Dharani')" 
