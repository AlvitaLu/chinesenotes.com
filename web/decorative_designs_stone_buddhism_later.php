        A <span class='bookTitle'>sutra pillar</span> <a href='/word_detail.php?id=26019' onmouseover="showToolTip(this, '經幢 jīngzhuàng', 'a sutra pillar')" 
        onmouseout='hideToolTip()'>经幢</a> is a stone column placed outside of temples, most often present in temples dating in 
        the Tang and before.
        Sutras or mantras are inscribed on them along with decorative carvings.
        The sutra pillar shown below is the West Sutra Pillar at Lingyin Temple.
        There are two sutra pillars in from of Lingyin Temple: one to the east and one to the west.
        The pillars are octagonal.
        The <span class='bookTitle'>Usnisa Vijaya Dharani</span> <a href='/word_detail.php?id=26043' onmouseover="showToolTip(this, '尊勝陀羅尼經 Zūnshèng Tuóluóní Jīng', 'Usnisa Vijaya Dharani / Uṣṇīṣa Vijaya Dhāraṇī')" 
