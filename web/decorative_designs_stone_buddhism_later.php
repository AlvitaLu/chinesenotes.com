      <p>
        Chinese <span class='bookTitle'>pagodas</span> <a href='/word_detail.php?id=3374' onmouseover="showToolTip(this, 'tǎ', 'a pagoda / a tower / a stupa')" 
        onmouseout="hideToolTip()">塔</a> originate from India <span class='bookTitle'>stupas</span>.
        The word in Chinese is the same in both cases.
        In English we use the term <span class='bookTitle'>stupa</span> for the original Indian style, built in the shape of an
        upturned alms bowl and still common in Thailand, Burma, and other South and Southeast Asian countries.
        Chinese pagodas tend to be taller and more slender and come in a number of different styles.
        The one shown below is a <span class='bookTitle'>stone pagoda</span> <a href='/word_detail.php?id=26023' onmouseover="showToolTip(this, 'shítǎ', 'a stone pagoda')" 
        onmouseout='hideToolTip()'>石塔</a> in Linyin Temple, Hangzhou.
        The detail on the right shows a <span class='bookTitle'>false door</span> <a href='/word_detail.php?id=26024' onmouseover="showToolTip(this, '假門 jià mén', 'a false door')" 
        onmouseout='hideToolTip()'>假门</a> carved on the outside of the pagoda.
        False windows and doors were common in early Indian stupas but have become less common in more recent Chinese pagodas.
        Their use relates to the practice of storing relics of monks and others within or under the pagodas.
      </p>
