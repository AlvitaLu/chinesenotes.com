       <p>
        <span class='bookTitle'>Maitreya Bodhisattva</span> <a href='/word_detail.php?id=26018' onmouseover="showToolTip(this, '彌勒菩薩 Mílè Púsà', 'Maitreya Bodhisattva')" 
        onmouseout='hideToolTip()'>弥勒菩萨</a> is one of the most commonly seen bodhisattvas in Chinese Buddhism.
        He will be the next Buddha of our world.
        Because he is a bodhisattva bringing great happiness and contentment he is often shown with a large belly in 
        Chinese Buddhism and statues of <span class='bookTitle'>Maitreya</span> are often placed in food halls
        and Chinese restaurants.
        The picture below shows the <span class='bookTitle'>Maitreya</span> statue at Feilai Peak, which is one of the best 
