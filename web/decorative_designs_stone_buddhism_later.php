        In one hand he is holding the cloth bag and in the other he is holding <span class='bookTitle'>prayer beads</span> <a href='/word_detail.php?id=17385' onmouseover="showToolTip(this, '唸珠 niàn zhū​', 'prayer beads / rosary')" 
        onmouseout='hideToolTip()'>念珠</a>.
        The cloth bag represents its contents: it is a lightweight bag that can hold things of any shape.
        <span class='bookTitle'>Maitreya</span> is flanked by 18 <span class='bookTitle'>Arhats</span> <a href='/word_detail.php?id=9041' onmouseover="showToolTip(this, '羅漢 Luóhàn', 'Arhat')" 
