        Copying
      </h2>
      <p>
        The most common approach when beginning calligraphy is to learn by copying 
        (<a href='/word_detail.php?id=16988' onmouseover="showToolTip(this, 'línmó', 'to copy')" onmouseout="hideToolTip()">临摹</a>)
        characters from worthy models, such as Yan Zhenqing. This is important to learn how
        the strokes are formed into characters and how strokes is modified to meet the 
        needs of each particular character.  Copybooks are valuable and easily found in 
        Chinese bookstores.  However, there are different ways to use them and they vary in
        quality and assumptions of prerequisite skills.
      </p>
