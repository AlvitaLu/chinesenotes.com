      <p>
        The principles of palace layout described in the Confucian class the 
        <span class='bookTitle'>Rites of Zhou</span> were used in the design of the Imperial Palace to 
        demonstrate that it was a part of the Mandate of Heaven.
        This followed the same pattern as imperial palaces in Luoyang in the Northern Wei, in Changan in the
        Tang, and in Kaifeng in the Norther Song.
        This is apparent in the naming of many halls in the palace.
        For example, the name of the <span class='bookTitle'>Taihe Hall</span> <a href='/word_detail.php?id=26556' onmouseover="showToolTip(this, 'Tàihé Diàn', 'Taihe Hall')" 
        onmouseout="hideToolTip()">太和殿</a> (Hall of Supreme Harmony), 
        which is the first and largest of the three great halls in the 
        Forbidden City, comes from the Book of Changes, and means the harmonious operation of all things on earth.
        It is 27 meters tall and occupies an area of approximately 2,400 square meters
        constructed on a three tier white marble base.
        Inside the hall is a gold lacquered dragon throne.
        It was the site of the most important imperial ceremonies, including the emperor's inauguration,
        birthday celebration, New Year, conferring of degrees from the Imperial College, and so on.
        Taihe Hall is shown below.
      </p>
