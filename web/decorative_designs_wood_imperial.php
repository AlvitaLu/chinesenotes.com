        <span class='bookTitle'>Jiaotai Hall</span> <a href='/word_detail.php?id=26564' onmouseover="showToolTip(this, 'Jiāotài Diàn', 'Jiaotai Hall')" 
        onmouseout="hideToolTip()">交泰殿</a>, and the 
        <span class='bookTitle'>Kunning Palace</span> <a href='/word_detail.php?id=26561' onmouseover="showToolTip(this, '坤寧宮 Kūnníng gōng', 'Kunning Palace')" 
        onmouseout='hideToolTip()'>坤宁宫</a>.
        The inner palace area is where most of the emperors lived with his emperesses and concubines. 
        In contrast to the stark and imposing outer court, the inner court had many gardens and pavillions for the
        imperial family to relax in their private lives.
        The Wan Shou Ting (longevity pavilion) from the inner court is shown below.
      </p>
