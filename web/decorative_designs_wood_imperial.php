      <p>
        In the picture above the <span class='bookTitle'>Imperial Boulevard</span> <a href='/word_detail.php?id=26567' onmouseover="showToolTip(this, 'Yùdào', 'The Imperial Boulevard')" 
        onmouseout="hideToolTip()">御道</a> leads up the steps along the central axis towards the 
        marble platform.
        The huge spaces inside the Forbidden City were used to contain a huge number of servants and officials
        and also to give an imposing feeling of granduer to the palace.
        Taihe Square, located in the area in front of Taihe Hall, with a total area of over 30,000 square meters,
        is the largest plaza inside a building structure in the world today.
      </p>
      <p>
        The <span class='bookTitle'>Zhonghe Hall</span> <a href='/word_detail.php?id=26557' onmouseover="showToolTip(this, 'Zhōnghé Diàn', 'Zhonghe Hall')" 
        onmouseout="hideToolTip()">中和殿</a> is the second of the three great halls.
        The name Zhonghe comes from the Book of Rites and means to handle matters properly and harmoniously without
        bias and all things will flourish.
        The emperor would meet with ministers here and stop here before going to the Taihe Hall.
        The apex of the roof has a ball-shaped structure called a <span class='bookTitle'>baoding</span> <a href='/word_detail.php?id=26558' onmouseover="showToolTip(this, '寶頂 bǎo dǐng', 'baoding')" 
        onmouseout="hideToolTip()">宝顶</a>.
        Most structures in the Forbidden City are based on the numbers five and nine, being the middle and greatest
        of the odd numbers les than ten.
        The Taihe Hall is nine bays wide and the Zhonghe Hall is five bays wide.
        The Zhonghe Hall is shown below.
      </p>
