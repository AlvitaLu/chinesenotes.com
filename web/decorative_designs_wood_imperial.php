      <h5 class='article'>
        <a name="gugong"></a>
        Imperial Palace
        <a href='/word_detail.php?id=7450' onmouseover="showToolTip(this, '故宮 Gù​gōng', 'The Imperial Palace')" 
        onmouseout="hideToolTip()">故宫</a>
      </h5>
      <p>
        The <span class='bookTitle'>Imperial Palace</span> <a href='/word_detail.php?id=7450' onmouseover="showToolTip(this, '故宮 Gù​gōng', 'The Imperial Palace')" 
        onmouseout="hideToolTip()">故宫</a>, also known as the <span class='bookTitle'>Forbidden City</span> <a href='/word_detail.php?id=8501' onmouseover="showToolTip(this, 'Zǐjì​nchéng', 'The Forbidden City / the Imperial Palace')" 
        onmouseout='hideToolTip()'>紫禁城</a> was home to 24 emperors of the Ming and Qing dynasties.
        The last emperor, Puyi, left the Forbidden City in 1924.
        It is located in the center of Beijing on Changan Avenue opposite Tiananmen Square.
        Construction of the Forbidden City began in 1420 during the reign of 
        <span class='bookTitle'>Emperor Yong​ Le</span> <a href='/word_detail.php?id=19643' onmouseover="showToolTip(this, '永樂 Yǒng​ Lè', 'Emperor Yong​ Le')" 
        onmouseout="hideToolTip()">永乐</a>.
        Over a hundred thousand skilled craftsmen and over a million laborers were used to build the palace.
        It is aligned around the north-south axis that extends outside the palace grounds to the 
        Bell and Drum Towers in the north and Yongdingmen in the south.
        With complex include 70 halls, 980 buildings, and more than 8,700 rooms it is the largest palace complex 
        in the world.
        The Forbidden City was the first historic area in China to be listed as a UNESCO World Cultural Heritage Site.
      </p>
      <p>
        Gates are a very important element in Chinese architecture.
        <span class='bookTitle'>Tiananmen</span> <a href='/word_detail.php?id=4016' onmouseover="showToolTip(this, '天安門 Tiānānmén', 'Tiananmen')" 
        onmouseout="hideToolTip()">天安门</a>, also known as the 
        <span class='bookTitle'>Gate of Heavenly Peace</span> is immediately south of the entrance to the 
        Forbidden City.
        It is were imperial edicts were announced.
        Tiananmen achieves this.
      </p>
