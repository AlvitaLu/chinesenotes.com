      <p>
        The third of the great halls is <span class='bookTitle'>Baohe Hall</span> <a href='/word_detail.php?id=26559' onmouseover="showToolTip(this, 'Bǎohé Diàn', 'Baohe Hall')" 
        onmouseout="hideToolTip()">保和殿</a>.
        During the Ming Dynasty the emperor changed clothes in this hall before important ceremonies.
        In the Qing Dynasty the emperor held banquets in the hall to entertain princes, dukes, and ministers of
        ethnic minorities.
        Emperors Kangxi and Shunzhi lived in the hall.
        Later, this was where the highest imperial examinations, ke-ju, were held.
        The name of the hall, Baohe, comes from the Book of Changes, meaning to maintain harmony between all things
        on earth to maintain a long period of peace.
        Baohe Hall is shown below.
      </p>
