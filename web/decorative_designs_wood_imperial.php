      <p>
        The three great halls are in the 
        <span class='bookTitle'>outer court</span> <a href='/word_detail.php?id=26566' onmouseover="showToolTip(this, 'wàicháo', 'outer court')" 
        onmouseout="hideToolTip()">外朝</a> area, where the emperor and high officials managed
        the affairs of state.  
        In this area the dragon is the primary symbol used on all artifacts.
        The area north of the Qianqing Gate is the 
        <span class='bookTitle'>inner court</span> <a href='/word_detail.php?id=26565' onmouseover="showToolTip(this, '內朝 nèi cháo', 'inner court')" 
        onmouseout="hideToolTip()">内朝</a> with the three main buildings being the
        <span class='bookTitle'>Qianqing Palace</span> <a href='/word_detail.php?id=26563' onmouseover="showToolTip(this, '乾清宮 Qiánqīng Gōng', 'Qianqing Palace ')" 
