      <h3 class='article'>
        <a name="textiles"></a>
        Textiles
        <a href='/word_detail.php?id=25629' onmouseover="showToolTip(this, '織物 zhī wù', 'cloth / woven material / textiles / fabric')" onmouseout="hideToolTip()">织物</a>
      </h3>
      <p>
        Textiles are a unique medium for decorative art.
        Decorations on clothes, in particular, have a close connection with the person wearing them and tell us
        a lot about the culture of the period.
      </p>
      <p>
        Embroidery was the favored way to add decorations to traditional clothes.
        Needlework as compulsory for women in China for over 2,000 years, as mandated by Confucian ethics. 
        In fact, embroidered silk articles were a major source of export revenue for China.
        The lady's jacket below shows a design with a 'hundred sons.' 
        Combined with the dragons on this lady's jacket, the meaning is to hope that the person wearing it would be a very 
        productive concubine for the emperor.
      </p>
