      <h2 class='article'>
       Clear Stream
      </h2>
      <div>
        <p class='source'>
          By Wang Wei (701 - 761 AD)， translation by Sun Dayu.
        </p>
      </div>
      <p>
        In boating to the Yellow Flower Stream,<br/>
        One should go coarsing on the Clear Steam down.<br/>
        To cover the distance short of a hundred li,<br/>
        The route windth round some ten thousand along.<br/>
        The water roareth among a chaos of rocks,<br/>
        The hills lie quiet beneath the thick pine trees.<br/>
        Afloat on the waves are trapas and nymphaeas,<br/>
        Reflected clearly are clumps of rushes and sedges.<br/>
        My heart has long been used to peace and calm;<br/>
        How this limpid stream is tranquil and pleasing!<br/>
        I wish to remain here on this massive rock,<br/>
        To spend my placid days in peace and angling.
      </p>
