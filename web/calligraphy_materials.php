      <h2 class='article'>
        <a name="materials"></a>
        <a href='/word_detail.php?id=11290' onmouseover="showToolTip(this, 'wénjù', 'stationery')" onmouseout="hideToolTip()">文具</a>
        Materials
      </h2>
      <p>
        The basic materials needed are:
      </p>
      <ol>
        <li>
          <a href='/word_detail.php?id=2594' onmouseover="showToolTip(this, 'máobǐ', 'writing brush')" onmouseout='hideToolTip()'>毛笔</a>
          Brush
          <p>
            The size will depend on the size of characters you want to write.  
            A good size of character for beginners to start with is 10 cm x 10 cm (4 in x 4 in).
            Smaller characters and larger characters are harder to write and also need different size brushes
            Brushes have size numbers but they depend on the brand.  
            Since there is no standard for this you will need to ask someone knowledgeable when buying
            a first set of brushes.  
          </p>
          <p>
            When starting with a new brush work some water into it to soften it.
          </p>
        </li>
        <li>
          <a href='/word_detail.php?id=17230' onmouseover="showToolTip(this, 'mòzhī', 'prepared Chinese ink')" onmouseout="hideToolTip()">墨汁</a>
          Ink
          <p>
            A prepared Chinese ink manufactured specially for calligraphy.  I have used ink manufactured by
            <a href='/word_detail.php?id=301' onmouseover="showToolTip(this, 'yī', 'one')" onmouseout="hideToolTip()">一</a><a href='/word_detail.php?id=411' onmouseover="showToolTip(this, 'de', 'a sentence particle used after a verb to show effect / degree or possibility')" onmouseout="hideToolTip()">得</a><a href='/word_detail.php?id=14223' onmouseover="showToolTip(this, 'gé', 'council-chamber / government cabinet')" onmouseout="hideToolTip()">阁</a>
            (<a href='http://www.yidege.net/'>www.yidege.net</a>).  You will need to mix the ink up with
            some water on the palette.  The amount of water depends on the shade you want the writing to be. 
          </p>
        </li>
        <li>
          <a href='/word_detail.php?id=2472' onmouseover="showToolTip(this, 'zhǐ', 'paper')" onmouseout='hideToolTip()'>纸</a>
          Paper
          <p>
            The correct type of paper that has a soft surface, absorb and hold the ink, and 
            has sufficient strength is important. 
            Although it is tempting to use cheaper paper for repetitious practice it can inhibit good style.
            The paper recommended for me to start with is made from bamboo starch.
            Having paper with printed squares on it is nice to help keep to drawing regular characters.
            A copy book <a href='/word_detail.php?id=17224' onmouseover="showToolTip(this, 'zìtiè', 'copybook')" onmouseout="hideToolTip()">字帖</a>
            or paper specially made for tracing can be very helpful for practicing.
          </p>
          <p>
            It is also nice to have a felt desk pad (<a href='/word_detail.php?id=17234' onmouseover="showToolTip(this, 'shūhuà zhān', 'felt desk pad')" onmouseout="hideToolTip()">书画毡</a>
            or <a href='/word_detail.php?id=17240' onmouseover="showToolTip(this, 'huàdiàn', 'a drawing pad')" onmouseout="hideToolTip()">画垫</a>)
            to provide a nice writing surface and prevent the ink from bleeding onto the table below.
            The printed squares on it can help with paper that has no printed outlines.
          </p>
        </li>
        <li>
          <a href='/word_detail.php?id=17229' onmouseover="showToolTip(this, 'tiáosè pán', 'a palette')" onmouseout="hideToolTip()">调色盘</a>
          Palette or plastic plate
          <p>
           This for working with ink and water to work it into the brush and prevent it from spilling.
          </p>
        </li>
      </ol>
