      <p>
        Some radicals look the same in printed fonts but are different in handwriting because of different strokes used.
        For example, the left and right ear radicals look the same in most printed fonts but are different in handwriting, 
        as shown below.  The left ear radical <a href='/word_detail.php?id=7039' onmouseover="showToolTip(this, '阜 fù​', 'Kangxi radical 170')" 
        onmouseout="hideToolTip()">阝</a>, called <a href='/word_detail.php?id=24671' onmouseover="showToolTip(this, 'zuǒ ěr páng', 'left ear radical / name of variant of Kangxi radical 163')" 
        onmouseout="hideToolTip()">左耳旁</a>, uses a 
        straight vertical stroke <a href='/word_detail.php?id=24581' onmouseover="showToolTip(this, '垂露豎 chuí lù​ shù', 'straight vertical stroke')" 
        onmouseout="hideToolTip()">垂露竖</a> with a regular ending but the right ear radical ⻏, called <a href='/word_detail.php?id=24670' onmouseover="showToolTip(this, 'yòu ěr páng', 'right ear radical / name of variant of Kangxi radical 163')" 
        onmouseout="hideToolTip()">右耳旁</a>, uses the hanging vertical stroke <a href='/word_detail.php?id=17337' onmouseover="showToolTip(this, '懸針 xuán zhēn', 'hanging vertical stroke')" 
        onmouseout='hideToolTip()'>悬针</a> with a pointed end.
      </p>
