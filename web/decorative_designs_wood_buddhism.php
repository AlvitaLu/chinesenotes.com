        Buddhist
        <a href='/word_detail.php?id=2890' onmouseover="showToolTip(this, 'Fójiào', 'Buddhism')" 
        onmouseout="hideToolTip()">佛教</a>
      </h3>
      <p>
        Buddhist temples are filled with decorative designs, both on the architectural structures and on
        wooden artifacts inside the halls.
        If these artifacts are used in ceremonies they are usually referred to as 
        <span class='bookTitle'>Dharma instruments</span> <a href='/word_detail.php?id=22163' onmouseover="showToolTip(this, 'fǎqì​', 'Dharma instruments / musical instruments used in Buddhist ceremonies')" 
        onmouseout="hideToolTip()">法器</a>.
        The <span class='bookTitle'>Dharma Wheel</span> <a href='/word_detail.php?id=17768' onmouseover="showToolTip(this, '法輪 fǎlún', 'Dharma Wheel / Dharmacakra')" 
        onmouseout="hideToolTip()">法轮</a> from the Sanskrit word <span class='bookTitle'>Dharmacakra</span> represents the 
        teachings of the Buddha.
        There are usually eight spokes representing the <span class='bookTitle'>Noble Eightfold Path</span> <a href='/word_detail.php?id=6173' onmouseover="showToolTip(this, 'Bā Zhèng Dào', 'Noble Eightfold Path / Eightfold Noble Way')" 
        onmouseout="hideToolTip()">八正道</a>.
      </p>
