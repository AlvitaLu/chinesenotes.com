        A <span class='bookTitle'>wooden fish</span> <a href='/word_detail.php?id=19763' onmouseover="showToolTip(this, '木魚 mù​ yú', 'a wooden fish')" 
        onmouseout='hideToolTip()'>木鱼</a> is a percussion instrument used in Buddhism.
        A fish, with eyes always open, symbolizes wakefulness.
        The wooden fish outside the food hall at Fayu Temple, Mount Putuo, shown below, is the largest one that I ever seen.
        This type of wooden fish is struck to let people know that it is dinner time.
