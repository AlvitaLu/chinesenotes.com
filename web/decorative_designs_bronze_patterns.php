          <div class='picture_header'>Animal Mask Design</div>
        </div>
      </div>
      <p>
        <span class='bookTitle'>Animal mask design</span> <a href='/word_detail.php?id=25275' onmouseover="showToolTip(this, '獸面紋 shòumiànwén', 'animal mask design')" 
        onmouseout="hideToolTip()">兽面纹</a>, also known as <span class='bookTitle'>Taotie design</span> <a href='/word_detail.php?id=25316' onmouseover="showToolTip(this, '饕餮紋 tāotiè wén', 'a zoomorphic mask motif / an kind of animal mask design')" 
        onmouseout='hideToolTip()'>饕餮纹</a>, is the most important design on Shang bronze ware and continued to be used in
        the Western Zhou.  It shows a ferocious mythical animal called a <span class='bookTitle'>taotie</span> <a href='/word_detail.php?id=25320' onmouseover="showToolTip(this, 'tāotiè', 'a ferocious mythological animal')" 
        onmouseout='hideToolTip()'>饕餮</a> that has a huge head and little or no body.
        The design was first described in <span class='bookTitle'>Mr Lu's Annals of the Spring and Autumn Period</span> <a href='/word_detail.php?id=17168' onmouseover="showToolTip(this, '呂氏春秋 Lǚ shì​ chū​n qiū​', 'Mr Lu&apos;s Annals of the Spring and Autumn Period')" 
        onmouseout='hideToolTip()'>吕氏春秋</a>, a historical record compiled in the Warring States Period.
        Unfortunately, we do not know the meaning of the <span class='bookTitle'>Taotie</span> today.
      </p>
      <a name="dragon"></a>
      <div class="picture">
