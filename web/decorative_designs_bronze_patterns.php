      <p>
        The <span class='bookTitle'>dragon design</span> <a href='/word_detail.php?id=25280' onmouseover="showToolTip(this, '龍紋 lóng wén', 'dragon design')" 
        onmouseout='hideToolTip()'>龙纹</a> appeared in the Shang or earlier.
        The dragon was worshipped as the god of water in ancient China.
        Rain was thought by some to be caused by dragons flying in the clouds.
        One of the most popular designs is the <span class='bookTitle'>kui dragon</span> <a href='/word_detail.php?id=25493' onmouseover="showToolTip(this, '夔龍 kuílóng', 'a kui dragaon')" 
        onmouseout="hideToolTip()">夔龙</a>, a dragon with a bowed body, horns, and large eyes, as shown above.
      </p>
