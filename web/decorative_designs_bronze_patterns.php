      <p>
        The <span class='bookTitle'>bird design</span> <a href='/word_detail.php?id=25495' onmouseover="showToolTip(this, '鳥紋 niǎo wén', 'bird design')" 
        onmouseout='hideToolTip()'>鸟纹</a> and <span class='bookTitle'>phoenix design</span> were popular in the 
        Shang and Zhou dynasties.
        The phoenix integrates features from different birds and is an auspicious animal.
        Originally, <span class='bookTitle'>bird designs</span> were a symbol of male reproductive organs.
        Originally, <span class='bookTitle'>bird designs</span> include <span class='bookTitle'>phoenix design</span> <a href='/word_detail.php?id=25496' onmouseover="showToolTip(this, '鳳紋 fèng wén', 'phoenix design')" 
        onmouseout='hideToolTip()'>凤纹</a>, <span class='bookTitle'>owl design</span> <a href='/word_detail.php?id=25499' onmouseover="showToolTip(this, '鴟梟紋 chīxiāo wén', 'owl design')" 
        onmouseout='hideToolTip()'>鸱枭纹</a>, <span class='bookTitle'>luan design</span> <a href='/word_detail.php?id=25501' onmouseover="showToolTip(this, '鸞紋 luán wén', 'luan design')" 
        onmouseout='hideToolTip()'>鸾纹</a> (based on the luan, a mythical bird), and <span class='bookTitle'>goose design</span> <a href='/word_detail.php?id=25502' onmouseover="showToolTip(this, '雁紋 yàn wén', 'goose design')" 
        onmouseout='hideToolTip()'>雁纹</a>.
      </p>
