      </h3>
      <p>
        Different patterns have been found and classified by archaeologists, including <span class='bookTitle'>fire design</span> 
        <a href='/word_detail.php?id=25277' onmouseover="showToolTip(this, '火紋 huǒ wén', 'fire designs')" 
        onmouseout='hideToolTip()'>火纹</a>, <span class='bookTitle'>lightening design</span> <a href='/word_detail.php?id=25278' onmouseover="showToolTip(this, '雷紋 léi wén', 'lightening designs')" 
        onmouseout='hideToolTip()'>雷纹</a>, <span class='bookTitle'>animal mask design</span> <a href='/word_detail.php?id=25275' onmouseover="showToolTip(this, '獸面紋 shòu miàn wén', 'animal mask design')" 
        onmouseout="hideToolTip()">兽面纹</a>, <span class='bookTitle'>bird design</span> <a href='/word_detail.php?id=25276' onmouseover="showToolTip(this, '鳳鳥紋 fèng niǎo wén', 'bird designs')" 
        onmouseout='hideToolTip()'>凤鸟纹</a>, <span class='bookTitle'>animal design</span> <a href='/word_detail.php?id=25279' onmouseover="showToolTip(this, '動物紋 dòngwù wén', 'animal designs')" 
        onmouseout='hideToolTip()'>动物纹</a>, and <span class='bookTitle'>dragon design</span> <a href='/word_detail.php?id=25280' onmouseover="showToolTip(this, '龍紋 lóng wén', 'dragon design')" 
        onmouseout='hideToolTip()'>龙纹</a>.
      </p>
      <a name="animal_mask"></a>
      <div class="picture">
        <a href='illustrations_use.php?mediumResolution=animal_mask.png'><img class='use' src='images/animal_mask.png' alt='animal mask design' title='animal mask design'/></a>
        <div>
          <div class='picture_header'><a href='/word_detail.php?id=25275' onmouseover="showToolTip(this, '獸面紋 shòumiànwén', 'animal mask design')" 
