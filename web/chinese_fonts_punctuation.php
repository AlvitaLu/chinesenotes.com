        Fullwidth and Halfwidth Forms
      </h3>
      <p>
        Chinese characters tend to be larger than Latin characters so to deal with mixing the two types of symbols together in text
        fullwidth and halfwidth forms have been developed.
        The fullwidth forms make Latin text seem highly spread out.
        The same problem applies to Katakana (Japanese) and Hangul (Korean) characters.
        As the name suggests, a fullwidth form is a Latin (Katakana or Hangul) character that takes up the full width of a 
        Chinese character.  A halfwidth from takes up half the width of a Chinese character.
        The difference is illustrated in the figure below.
      </p>
      <div class="picture">
        <img src='images/fullwidth.png' alt='Fullwidth and Halfwidth Forms' title='Fullwidth and Halfwidth Forms'/> 
