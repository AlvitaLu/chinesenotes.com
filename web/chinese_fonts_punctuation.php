        Vocabulary
      </h3>
      <p>
        How many characters is there and how many is enough?  The 
        <span class='bookTitle'>Commonly Used Characters in Modern Chinese</span>
        [<a href="chinese_fonts_ref.php">Ministry of Education of the People's Republic of China 1988</a>] defines 2,500 commonly 
        used (simplified) characters and another 1,000 secondarily commonly used characters.  To arrive at this number the Ministry of 
        Education analyzed the People's Daily and other major newspapers over a period of several years and found that the 2,500
        commonly used characters formed 97.7% of the text and the 1,000 secondarily commonly used characters formed 1.51%, or
        99.48% total for the combined 3,500 characters.  
        [<a href="chinese_fonts_ref.php">Jun Da 2004</a>] lists the frequency of use this set of characters in a range of texts.
        Readers of literature, historic, medical, and scientific texts will need to know more than this number.
        The <span class='bookTitle'>Kangxi Dictionary</span> [<a href="chinese_fonts_ref.php">Zhang Yushu and Chen Tingjing 1716</a>],
        considered the first modern Chinese dictionary, defines over 47,000 characters.
        The <span class='bookTitle'>Hanyu Da Zidian</span>
        [<a href="chinese_fonts_ref.php">Hanyu Da Zidian Editorial Committee 1995</a>] defines 54,678 characters.
      </p>
      <p>
        Today, most words in Chinese have two characters.
        Chinese went through a transition from predominantly single character words to predominantly two character words.
        Making up new characters for each word meant that too many characters to keep track of were invented.
        Also, with the one-to-one relation between character and syllable, the spoken language was becoming too ambiguous.
        So gradually more two character words were introduced to express more concepts and give more precision to their meaning.
        Some interesting relics are left in the language from this transition.
        For example, the character <a href='/character_search.php?unicode=31873' onmouseover="showToolTip(this, 'qiān', 'a kilometer')" 
        onmouseout="hideToolTip()">粁</a> is an archaic form for kilometer that combines the rice radical <a href='/character_search.php?unicode=31859' onmouseover="showToolTip(this, 'mǐ', 'hulled or husked uncooked rice')" 
        onmouseout="hideToolTip()">米</a>, which also means meter, with <a href='/character_search.php?unicode=21315' onmouseover="showToolTip(this, 'qiān 仟', 'a thousand / many / numerous / very / a cheater / a swindler')" 
        onmouseout="hideToolTip()">千</a>, meaning a thousand, together to mean a thousand meters.
      </p>
