    <h1>About 关于本网站</h1>
    <p>
      Please send ideas and opinions to alex@chinesenotes.com or contact me for help relating to any
      of the topics or tools on this web site.  Feel free to post comments on my blog at 
      <a href='http://zhongwenbiji.blogspot.com/'>zhongwenbiji.blogspot.com</a>.
      I update the blog from time to time with changes that I have made to the site or for 
      some background about the content on the site.
    </p>
    <p>
       这个网站是为了给学中文的外国人创立的。话题包括时事双言蚊帐，语言工具，词汇目录，参考资料，佛经，古诗，开发网站的资料，等。双言文章包括实事，经济，商务，体育，科学，科技，环保等。我希望在本网站你能找到使用，能帮助你的资料。 
    </p>
    <p>
      This website was created as a resource for people
      interested in learning Mandarin Chinese. 
      The content includes a Chinese-English dictionary, bilingual (Chinese / English) 
      current affairs articles, language tools, vocabulary lists, 
      reference materials, Buddhist sutras, classical Chinese poetry, and resources for developers of 
      bilingual sites.  
      Bilingual articles include current affairs, business, economics, sports, science, technology, and the environment.
      I hope that you will be able to find resources on this web site that can help
      you in a practical way.
    </p>
