      <h2 class='article'>
        <a name="writing"></a>
        <a href='/word_detail.php?id=17239' onmouseover="showToolTip(this, 'zhíbǐ', 'writing technique / holding the brush')" onmouseout="hideToolTip()">执笔</a><a href='/word_detail.php?id=509' onmouseover="showToolTip(this, 'yǔ', 'and')" onmouseout="hideToolTip()">与</a><a href='/word_detail.php?id=17261' onmouseover="showToolTip(this, 'yùnbǐ', 'to move a brush / to move a pen')" onmouseout="hideToolTip()">运笔</a>
        Holding and Moving the Brush
      </h2>
      <div class='fright'>
        <a href='illustrations_use.php?mediumResolution=holding_brush_med.jpg'><img class='use' 
           src='images/holding_brush_med.jpg' alt='执笔 Holding the Brush' 
           title='执笔 Holding the Brush'/></a>
      </div>
      <p>
        The correct way to hold the brush (<a href='/word_detail.php?id=17239' onmouseover="showToolTip(this, 'zhíbǐ', 'Holding the Brush')" onmouseout="hideToolTip()">执笔</a>)
        is vertically without resting your arm or wrist on the table.
        To start with your hand may shake but with a little practice you will be able to grasp it
        steadily.  Place your thumb on one side of the brush opposing your index and middle fingers.
        Your third and forth fingers should be on the same side as your thumb.  Hold your fingers 
        (<a href='/word_detail.php?id=11019' onmouseover="showToolTip(this, 'shǒuzhǐ', 'finger')" onmouseout="hideToolTip()">手指</a>)
        close together without tension and bend your wrist (<a href='/word_detail.php?id=7694' onmouseover="showToolTip(this, 'shǒuwàn', 'wrist')" onmouseout='hideToolTip()'>手腕</a>) upwards.
      </p>
      <p>
        The five characters <a href='/word_detail.php?id=17265' onmouseover="showToolTip(this, 'yǎn', 'to press down')" onmouseout="hideToolTip()">擫</a>
        (press)，<a href='/word_detail.php?id=3294' onmouseover="showToolTip(this, 'yā', 'to press / to push down')" onmouseout="hideToolTip()">压</a>
        (push)，<a href='/word_detail.php?id=12169' onmouseover="showToolTip(this, 'gōu', 'to hook')" onmouseout="hideToolTip()">钩</a>
        (hook)，<a href='/word_detail.php?id=3481' onmouseover="showToolTip(this, 'gé', 'square / frame / rule / (legal) case / style / character / standard / pattern')" onmouseout="hideToolTip()">格</a>
        (level)，and <a href='/word_detail.php?id=5614' onmouseover="showToolTip(this, 'dǐ', 'to hold up / to push against / to support / to resist / to reach / to arrive')" onmouseout="hideToolTip()">抵</a>
        (support) are used to describe the use of each finger to properly hold the brush.
        <a href='/word_detail.php?id=17265' onmouseover="showToolTip(this, 'yǎn', 'to press down')" onmouseout="hideToolTip()">擫</a>
        indicates that the thumb (<a href='/word_detail.php?id=17267' onmouseover="showToolTip(this, 'mǔzhǐ', 'thumb / big toe')" onmouseout="hideToolTip()">拇指</a>)
        should press against the brush handle (<a href='/word_detail.php?id=17269' onmouseover="showToolTip(this, 'bǐguǎn', 'brush handle / pen shaft')" onmouseout="hideToolTip()">笔管</a>)
        in an upward slanting position. <a href='/word_detail.php?id=3294' onmouseover="showToolTip(this, 'yā', 'to press / to push down')" onmouseout="hideToolTip()">压</a>
        indicates that the index finger or forefinger (<a href='/word_detail.php?id=17270' onmouseover="showToolTip(this, 'shízhǐ', 'index finger')" onmouseout="hideToolTip()">食指</a>)
        should be used in oppostion to the thumb to grasp the brush.
        <a href='/word_detail.php?id=12169' onmouseover="showToolTip(this, 'gōu', 'to hook')" onmouseout="hideToolTip()">钩</a>
        indicates that the middle finger (<a href='/word_detail.php?id=17271' onmouseover="showToolTip(this, 'zhōngzhǐ', 'middle finger')" onmouseout="hideToolTip()">中指</a>)
        grabs and wraps around (<a href='/word_detail.php?id=17272' onmouseover="showToolTip(this, 'wānqū', 'to wind / to wrap')" onmouseout='hideToolTip()'>弯曲</a>) 
        the brush.
        <a href='/word_detail.php?id=3481' onmouseover="showToolTip(this, 'gé', 'square / frame / rule / (legal) case / style / character / standard / pattern')" onmouseout="hideToolTip()">格</a>
        indicates that back of the ring finger is used to straighten the brush.
        <a href='/word_detail.php?id=5614' onmouseover="showToolTip(this, 'dǐ', 'to hold up / to push against / to support / to resist / to reach / to arrive')" onmouseout="hideToolTip()">抵</a>
        indicates that the little finger (<a href='/word_detail.php?id=17274' onmouseover="showToolTip(this, 'xiǎozhǐ', 'little finger')" onmouseout="hideToolTip()">小指</a>)
        supports the other fingers.  Your thumb and fingers should act together to hold the brush
        firmly but relaxed.
      </p>
      <p>
        Brush movement
        <a href='/word_detail.php?id=17261' onmouseover="showToolTip(this, 'yùnbǐ', 'to move a brush / to move a pen')" onmouseout="hideToolTip()">运笔</a>
        is achieved with the wrist and technique for holding the brush described above. 
      </p>
