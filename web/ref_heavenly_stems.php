        However, these ten symbols forming the heavenly stems should be used with the related earthly 
        branches: the heavenly stems as the main trunk and the earth branches forming the branches.
        The ancient Chinese used a type of device like a zipper to combine the stems and branches.  
        One one side the heavenly stems rotated and on the other the earthly brances rotated. Together 
        they form a cycle with the least common multiple of 60.
