      <p>
        Large numbers implying nearly infinite quantities are common and important in Buddhism.
        The steele below is titled <span class='bookTitle'>Ten thousand Buddhas Steele</span>, not meaning ten thousand literally
        but a meaning a very large number.
        In China, ten thousand traditionally represents a large number or infinity.
        In Buddhist sutras this concept was represented by the number of grains of sand in the Ganges, the number of dust particles 
        created by turning a world into dust (Lotus Sutra), the amount of space in a particular direction (Diamond Sutra),
        or other analogies. 
      </p>
