      <p>
        The statues above and below include a scenes describing <span class='bookTitle'>jataka stories</span> <a href='/word_detail.php?id=25751' onmouseover="showToolTip(this, 'běnshēng', 'a jataka story')" 
        onmouseout="hideToolTip()">本生</a>, which are stories from the life or a past life of the Buddha.
        They originate in Indian Buddhist art.
        The relief carving here is a mural style on a flat surface.
        This type of relief sculpture is produced by a multi-step process.  In the first step a painter paints the design
        on the stone.  In the second step a mason chisels lines around the outlines.
        In the third step the painter may have painted the stone.
      </p>
