      <p>
        An important development in the Tang was the use of multi-colored glaze, also known as tri-colored glaze or 
        <span class='bookTitle'>sancai</span> <a href='/word_detail.php?id=25744' onmouseover="showToolTip(this, 'sān cǎi', 'wucai / five colors / multi-colored')" 
        onmouseout='hideToolTip()'>三彩</a>.  This was quite different to the green celedon that preceded it.
        The glaze was a low temperature glaze mixed with iron, copper, manganese, cobolt, and other metals for coloring
        and then fired to about 750 to 800 degrees Celsius.
        The artifacts above and below are Tang <span class='bookTitle'>sancai</span> porcelain.
      </p>
