      <h3 class='article'>
        <a name="tang"></a>
        Tang Dynasty
        <a href='/word_detail.php?id=8299' onmouseover="showToolTip(this, 'Táng', 'Tang Dynasty')" onmouseout='hideToolTip()'>唐</a>
      </h3>
      <p>
        During the Tang (618&mdash;907) a technique called underglaize painting was developed.
        With this technique designs were painted on the roughcast and then glaze was applied over the top.
        White porcelain was developed in the Norther Qi (550&mdash;577) using pure whie roughcasts and glaze from reducing
        the iron content in the roughcast.
        The roughcasts and glaze is opal in color and the glaze of some is slightly blue.
        Pure white porcelain was developed in the Tang. 
        The <span class='bookTitle'>Yue Kiln</span> <a href='/word_detail.php?id=25597' onmouseover="showToolTip(this, '越窯 Yuè Yáo', 'Yue Kiln')" 
        onmouseout="hideToolTip()">越窑</a> in Zhejiang was designated as an imperial kiln and people outside of the imperial
        family were forbidden to use porcelain made there.
      </p>
