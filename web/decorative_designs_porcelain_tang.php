      <p>
        Marbled or <span class='bookTitle'>jiaotai</span> <a href='/word_detail.php?id=25721' onmouseover="showToolTip(this, '絞胎 jiǎotāi', 'marbled')" 
        onmouseout='hideToolTip()'>绞胎</a> pottery was developed in the Tang.
        In this technique alternating layers of brown and white clay are sandwiched together.
        Then slices are cut from the sandwich and used to create the utensil.
        The utensil is then glazed and fired.
        A number of variations are possible using this technique.
        A <span class='bookTitle'>marbled pottery</span> pot is shown below.
      </p>
