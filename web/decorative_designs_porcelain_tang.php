      <p>
        The wild goose is supposedly the bird of the <span class='bookTitle'>yang</span> <a href='/word_detail.php?id=3193' onmouseover="showToolTip(this, '陽 yáng', 'Yang / male principle')" 
        onmouseout='hideToolTip()'>阳</a> (male) principle.
        It is also sometimes used in reference to a story of <span class='bookTitle'>Su Wu</span> <a href='/word_detail.php?id=26151' onmouseover="showToolTip(this, '蘇武 Sū Wǔ', 'Su Wu')" 
        onmouseout="hideToolTip()">苏武</a> (140&mdash;60 BCE), a Han envoy to the Xiongnu who was kept captive for 20 years
        and informed emperor Han Wu Di of his location by attaching a letter to the leg of a wild goose.
      </p>
